//! Spec-compliance tests: titlePg + First-kind Inheritance Interaction
//! (ISO 29500-1 sections 17.10.6 + 17.10.2)
//!
//! These tests verify the interaction between titlePg (which controls only
//! whether the title-page header/footer is DISPLAYED for a section) and
//! per-kind header/footer inheritance across sections.
//!
//! titlePg gates DISPLAY, not file CONTENT: the parsed model carries First-kind
//! refs and stories verbatim regardless of titlePg, and lets the consumer
//! (Word) apply the display rule from `title_page` at render time. Inherited
//! First-kind refs persist through the chain even when sections lack titlePg.
//!
//! Fixture DOCX files are generated by
//! `testdata/spec-compliance/section-titlepg-interaction/create_fixtures.py`.

use stemma::{BlockNode, CanonDoc, HeaderFooterKind, InlineNode, SectionProperties, TrackedBlock};

use crate::common;

const FIXTURES: &str = "testdata/spec-compliance/section-titlepg-interaction";

// -- helpers ----------------------------------------------------------------

/// Extract plain text from tracked blocks.
fn blocks_text(blocks: &[TrackedBlock]) -> String {
    let mut text = String::new();
    for tb in blocks {
        if let BlockNode::Paragraph(p) = &tb.block {
            for inline in p.all_inlines() {
                if let InlineNode::Text(t) = inline {
                    text.push_str(&t.text);
                }
            }
        }
    }
    text
}

/// Collect all mid-document section properties (from pPr/sectPr).
fn section_paragraphs(doc: &CanonDoc) -> Vec<&SectionProperties> {
    let mut props = Vec::new();
    collect_section_props_tracked(&doc.blocks, &mut props);
    props
}

fn collect_section_props_tracked<'a>(
    blocks: &'a [TrackedBlock],
    out: &mut Vec<&'a SectionProperties>,
) {
    for tracked in blocks {
        match &tracked.block {
            BlockNode::Paragraph(p) => {
                if let Some(ref sp) = p.section_properties {
                    out.push(sp);
                }
            }
            BlockNode::Table(t) => {
                for row in &t.rows {
                    for cell in &row.cells {
                        collect_section_props_bare(&cell.blocks, out);
                    }
                }
            }
            BlockNode::OpaqueBlock(_) => {}
        }
    }
}

fn collect_section_props_bare<'a>(blocks: &'a [BlockNode], out: &mut Vec<&'a SectionProperties>) {
    for block in blocks {
        match block {
            BlockNode::Paragraph(p) => {
                if let Some(ref sp) = p.section_properties {
                    out.push(sp);
                }
            }
            BlockNode::Table(t) => {
                for row in &t.rows {
                    for cell in &row.cells {
                        collect_section_props_bare(&cell.blocks, out);
                    }
                }
            }
            BlockNode::OpaqueBlock(_) => {}
        }
    }
}

