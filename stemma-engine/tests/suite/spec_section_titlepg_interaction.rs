// S1 (mid-doc): titlePg=true, Default+First headers+footers
// S2 (mid-doc): no titlePg, no declarations (inherits from S1)
// S3 (body-level): titlePg=true, no declarations (inherits from S2)
//
// After inheritance + filtering:
//   S1: Default+First (titlePg, kept)
//   S2: Default only (no titlePg, First filtered)
//   S3: Default+First (titlePg, kept -- First inherited via S2 before filter)
// ==========================================================================

/// S1 should have both Default and First header refs (titlePg=true).
#[test]
fn gap_s1_has_default_and_first_header_refs() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "titlepg-gap-inheritance");

    let sections = section_paragraphs(&doc);
    assert!(
        sections.len() >= 2,
        "should have at least 2 mid-document sections, got {}",
        sections.len()
    );

    let s1 = sections[0];
    assert_eq!(
        s1.title_page,
        Some(true),
        "S1 should have titlePg=true, got {:?}",
        s1.title_page
    );

    let has_default = s1
        .header_refs
        .iter()
        .any(|r| r.kind == HeaderFooterKind::Default);
    let has_first = s1
        .header_refs
        .iter()
        .any(|r| r.kind == HeaderFooterKind::First);
    assert!(has_default, "S1 should have Default header ref");
    assert!(has_first, "S1 should have First header ref (titlePg=true)");
}

