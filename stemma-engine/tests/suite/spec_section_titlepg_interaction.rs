/// S2 should have inherited Default + First header refs (titlePg=true).
#[test]
fn later_titlepg_s2_has_default_and_first_header_refs() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "titlepg-only-on-later-section");

    let body_sp = doc
        .body_section_properties
        .as_ref()
        .expect("body_section_properties should exist (S2)");

    assert_eq!(
        body_sp.title_page,
        Some(true),
        "S2 should have titlePg=true, got {:?}",
        body_sp.title_page
    );

    let has_default = body_sp
        .header_refs
        .iter()
        .any(|r| r.kind == HeaderFooterKind::Default);
    let has_first = body_sp
        .header_refs
        .iter()
        .any(|r| r.kind == HeaderFooterKind::First);
    assert!(
        has_default,
        "S2 should have inherited Default header ref from S1"
    );
    assert!(
        has_first,
        "S2 (titlePg=true) should have inherited First header ref from S1"
    );
}

