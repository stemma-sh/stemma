// Both S1 and S2 carry the First refs (titlePg gates display, not content):
// S1 declares them, S2 has titlePg=true. First-kind stories preserved.
// ==========================================================================

/// S1 (declares First refs) keeps its First header ref even with no titlePg —
/// titlePg gates DISPLAY, not the stored model (ISO 29500-1 §17.10.6).
#[test]
fn later_titlepg_s1_keeps_first_header_ref() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "titlepg-only-on-later-section");

    let sections = section_paragraphs(&doc);
    assert!(
        !sections.is_empty(),
        "should have at least one mid-document section"
    );

    let s1 = sections[0];
    let has_first = s1
        .header_refs
        .iter()
        .any(|r| r.kind == HeaderFooterKind::First);
    assert!(
        has_first,
        "S1's declared First header ref must be preserved even with no titlePg \
         (display is gated by title_page, not by mutating the model)"
    );
}

