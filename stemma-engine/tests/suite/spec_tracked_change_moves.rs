// ── Test 8: Import preserves pre-existing move tracking ─────────────────

/// ECMA-376 §17.13.5.21-28: When importing a DOCX that already has
/// moveFrom/moveTo tracked changes linked by moveFromRangeStart/moveToRangeStart
/// with matching w:name, the canonical model should preserve:
/// - TrackingStatus::Deleted for moveFrom blocks
/// - TrackingStatus::Inserted for moveTo blocks
/// - Matching move_id on both, derived from w:name
///
/// Fixture has:
///   P1: "Paragraph Alpha stays in place."      (Normal)
///   P2: moveTo[name="move_0"]: "Paragraph Charlie was moved here." (Inserted, move_id="move_0")
///   P3: "Paragraph Bravo stays in place."      (Normal)
///   P4: moveFrom[name="move_0"]: "Paragraph Charlie was moved here." (Deleted, move_id="move_0")
#[test]
fn spec_import_preserves_preexisting_moves() {
    let (_runtime, doc) = import_fixture_canonical("preexisting-moves");

    // Should have 4 blocks total
    assert!(
        doc.blocks.len() >= 4,
        "should have at least 4 blocks, got {}",
        doc.blocks.len()
    );

    // Find blocks with move_id set
    let move_blocks: Vec<&stemma::TrackedBlock> =
        doc.blocks.iter().filter(|b| b.move_id.is_some()).collect();

    assert_eq!(
        move_blocks.len(),
        2,
        "should have exactly 2 blocks with move_id (one moveFrom, one moveTo). \
         Got {} blocks with move_id. Block statuses: {:?}",
        move_blocks.len(),
        doc.blocks
            .iter()
            .map(|b| format!(
                "status={:?} move_id={:?}",
                std::mem::discriminant(&b.status),
                b.move_id
            ))
            .collect::<Vec<_>>()
    );

    // Both should share the same move_id
    let id0 = move_blocks[0].move_id.as_ref().unwrap();
    let id1 = move_blocks[1].move_id.as_ref().unwrap();
    assert_eq!(
        id0, id1,
        "moveFrom and moveTo blocks should share the same move_id. Got '{}' and '{}'",
        id0, id1
    );
    assert_eq!(
        id0, "move_0",
        "move_id should be 'move_0' (from w:name attribute), got '{}'",
        id0
    );

    // One should be Deleted (moveFrom), one should be Inserted (moveTo)
    let has_deleted = move_blocks
        .iter()
        .any(|b| matches!(b.status, TrackingStatus::Deleted(_)));
    let has_inserted = move_blocks
        .iter()
        .any(|b| matches!(b.status, TrackingStatus::Inserted(_)));

    assert!(has_deleted, "should have a Deleted block (from moveFrom)");
    assert!(has_inserted, "should have an Inserted block (from moveTo)");

    // Verify RevisionInfo is preserved on the move blocks
    for mb in &move_blocks {
        match &mb.status {
            TrackingStatus::InsertedThenDeleted(_) => {
                unreachable!("move blocks are never stacked")
            }
            TrackingStatus::Deleted(rev) | TrackingStatus::Inserted(rev) => {
                assert_eq!(
                    rev.author.as_deref(),
                    Some("Move Author"),
                    "move block RevisionInfo author should be 'Move Author'"
                );
                assert_eq!(
                    rev.date.as_deref(),
                    Some("2025-06-01T12:00:00Z"),
                    "move block RevisionInfo date should be '2025-06-01T12:00:00Z'"
                );
            }
            TrackingStatus::Normal => panic!("move block should not have Normal status"),
        }
    }
}

// ── Test 9: Move roundtrip fidelity ─────────────────────────────────────

/// Verify that the diff → serialize → re-import pipeline preserves move
/// tracking: re-imported blocks should retain move_id, and re-exported XML
/// should contain moveFrom/moveTo (not plain del/ins).
#[test]
fn spec_move_roundtrip_fidelity() {
    let before_path = format!("{FIXTURES}/reordered-paragraphs/before.docx");
    let after_path = format!("{FIXTURES}/reordered-paragraphs/after.docx");

    // Phase 1: diff and produce redline with move tracking
    let (_runtime, exported) = redline_from_pair(&before_path, &after_path);

    // Verify the exported XML has moveFrom/moveTo (sanity check)
    let xml1 = extract_document_xml(&exported);
    let root1 = parse_xml(&xml1);
    let mut mf1 = Vec::new();
    find_all_elements(&root1, "moveFrom", &mut mf1);
    assert!(
        !mf1.is_empty(),
        "phase 1: exported redline should contain moveFrom elements"
    );

    // Phase 2: re-import the redline DOCX
    let runtime2 = SimpleRuntime::new();
    let import2 = runtime2.import_docx(&exported).expect("re-import redline");

    // The canonical model should have blocks with move_id set
    let move_blocks: Vec<&stemma::TrackedBlock> = import2
        .canonical
        .blocks
        .iter()
        .filter(|b| b.move_id.is_some())
        .collect();

    assert!(
        move_blocks.len() >= 2,
        "re-imported redline should preserve move_id on at least 2 blocks \
         (one moveFrom source, one moveTo destination). Got {} blocks with move_id.",
        move_blocks.len()
    );

    // Verify move_id pairs: each move_id should appear on exactly one Deleted
    // and one Inserted block
    let mut move_ids: HashMap<&str, (bool, bool)> = HashMap::new();
    for mb in &move_blocks {
        let mid = mb.move_id.as_ref().unwrap().as_str();
        let entry = move_ids.entry(mid).or_insert((false, false));
        match &mb.status {
            TrackingStatus::Deleted(_) => entry.0 = true,
            TrackingStatus::Inserted(_) => entry.1 = true,
            TrackingStatus::Normal => {}
            TrackingStatus::InsertedThenDeleted(_) => {
                unreachable!("move blocks are never stacked")
            }
        }
    }

    for (mid, (has_del, has_ins)) in &move_ids {
        assert!(
            *has_del && *has_ins,
            "move_id '{}' should have both a Deleted (moveFrom) and Inserted (moveTo) block. \
             has_deleted={}, has_inserted={}",
            mid,
            has_del,
            has_ins
        );
    }

    // Phase 3: re-export and verify XML still uses moveFrom/moveTo
    let exported2 = runtime2
        .export_docx(&import2.doc_handle, ExportMode::Redline)
        .expect("re-export redline");

    let xml2 = extract_document_xml(&exported2);
    let root2 = parse_xml(&xml2);

    let mut move_froms2 = Vec::new();
    find_all_elements(&root2, "moveFrom", &mut move_froms2);
    let mut move_tos2 = Vec::new();
    find_all_elements(&root2, "moveTo", &mut move_tos2);
    let mut mfr_starts2 = Vec::new();
    find_all_elements(&root2, "moveFromRangeStart", &mut mfr_starts2);
    let mut mtr_starts2 = Vec::new();
    find_all_elements(&root2, "moveToRangeStart", &mut mtr_starts2);

    assert!(
        !move_froms2.is_empty(),
        "re-exported XML should still contain moveFrom elements (not plain del). \
         XML snippet: {}",
        &xml2[..xml2.len().min(2000)]
    );
    assert!(
        !move_tos2.is_empty(),
        "re-exported XML should still contain moveTo elements (not plain ins)"
    );
    assert!(
        !mfr_starts2.is_empty(),
        "re-exported XML should contain moveFromRangeStart markers"
    );
    assert!(
        !mtr_starts2.is_empty(),
        "re-exported XML should contain moveToRangeStart markers"
    );

    // Verify the range markers are linked by matching name
    let from_names2: HashSet<&str> = mfr_starts2
        .iter()
        .filter_map(|el| attr_value(el, "w:name"))
        .collect();
    let to_names2: HashSet<&str> = mtr_starts2
        .iter()
        .filter_map(|el| attr_value(el, "w:name"))
        .collect();
    let common2: HashSet<&&str> = from_names2.intersection(&to_names2).collect();

    assert!(
        !common2.is_empty(),
        "re-exported moveFromRangeStart and moveToRangeStart should share at least one w:name. \
         From: {:?}, To: {:?}",
        from_names2,
        to_names2
    );
}
