use crate::common;

const FIXTURES: &str = "testdata/spec-compliance/tracked-change-moves";

// ── Helpers ──────────────────────────────────────────────────────────────

fn redline_from_pair(before_path: &str, after_path: &str) -> (SimpleRuntime, Vec<u8>) {
    let before = fs::read(before_path).unwrap_or_else(|e| panic!("read {before_path}: {e}"));
    let after = fs::read(after_path).unwrap_or_else(|e| panic!("read {after_path}: {e}"));

    let runtime = SimpleRuntime::new();
    let ib = runtime.import_docx(&before).expect("import before");
    let ia = runtime.import_docx(&after).expect("import after");

    let meta = TransactionMeta {
        author: "spec_tracked_change_moves".to_string(),
        reason: Some("spec move tracking test".to_string()),
        timestamp_utc: Some("2025-06-01T12:00:00Z".to_string()),
    };
    let apply = runtime
        .diff_and_redline(&ib.doc_handle, &ia.doc_handle, meta)
        .expect("diff_and_redline");
    assert!(apply.applied, "redline should be applied");

    let exported = runtime
        .export_docx(&ib.doc_handle, ExportMode::Redline)
        .expect("export redline");
    (runtime, exported)
}

fn extract_document_xml(docx_bytes: &[u8]) -> String {
    let cursor = Cursor::new(docx_bytes);
    let mut zip = ZipArchive::new(cursor).expect("open DOCX zip");
    let mut file = zip
        .by_name("word/document.xml")
        .expect("DOCX must contain word/document.xml");
    let mut out = String::new();
    file.read_to_string(&mut out)
        .expect("read word/document.xml");
    out
}

fn parse_xml(xml: &str) -> Element {
    Element::parse(Cursor::new(xml.as_bytes())).expect("parse XML")
}

fn local_name(name: &str) -> &str {
    match name.rsplit_once(':') {
        Some((_, local)) => local,
        None => name,
    }
}

fn is_w_tag(element: &Element, local: &str) -> bool {
    let name = local_name(&element.name);
    if name != local {
        return false;
    }
    if element.prefix.as_deref() == Some("w") {
        return true;
    }
    let word_ns = "http://schemas.openxmlformats.org/wordprocessingml/2006/main";
    if element.namespace.as_deref() == Some(word_ns) {
        return true;
    }
    element.name == local || element.name == format!("w:{local}")
}

/// Collect all elements matching a given local name, recursively.
fn find_all_elements<'a>(root: &'a Element, tag: &str, out: &mut Vec<&'a Element>) {
    if is_w_tag(root, tag) {
        out.push(root);
    }
    for child in &root.children {
        if let XMLNode::Element(el) = child {
            find_all_elements(el, tag, out);
        }
    }
}

/// Get an attribute value, trying both prefixed and unprefixed forms.
fn attr_value<'a>(element: &'a Element, qname: &str) -> Option<&'a str> {
    // Try prefixed first (e.g. "w:id")
    let (prefix, local) = if let Some(pos) = qname.find(':') {
        (Some(&qname[..pos]), &qname[pos + 1..])
    } else {
        (None, qname)
    };

    if let Some(want_prefix) = prefix {
        for (name, value) in &element.attributes {
            if name.local_name == local && name.prefix.as_deref() == Some(want_prefix) {
                return Some(value);
            }
        }
    }

    // Fallback: match by local name only
    for (name, value) in &element.attributes {
        if name.local_name == local {
            return Some(value);
        }
    }

    None
}

/// Import a fixture DOCX and return the canonical model (preserving tracked changes).
fn import_fixture_canonical(name: &str) -> (SimpleRuntime, stemma::CanonDoc) {
    let path = format!("{FIXTURES}/{name}/input.docx");
    let bytes = fs::read(&path).unwrap_or_else(|e| panic!("read {path}: {e}"));
    let runtime = SimpleRuntime::new();
    let import = runtime
        .import_docx(&bytes)
        .unwrap_or_else(|e| panic!("import {path}: {e:?}"));
    (runtime, std::sync::Arc::unwrap_or_clone(import.canonical))
}

// ── Test 1: Move detection for reordered paragraphs ─────────────────────

/// ECMA-376 §17.13.5.22-28: When paragraphs are reordered between old and new
/// docs, the diff engine should detect the move and produce moveFrom/moveTo
/// markup (not just del+ins).
///
/// Old: "Paragraph Alpha" / "Paragraph Bravo" / "Paragraph Charlie"
/// New: "Paragraph Alpha" / "Paragraph Charlie" / "Paragraph Bravo"
///
/// The paragraphs have >20 characters, exceeding the move detection threshold.
#[test]
fn spec_move_detection_reordered_paragraphs() {
    let before_path = format!("{FIXTURES}/reordered-paragraphs/before.docx");
    let after_path = format!("{FIXTURES}/reordered-paragraphs/after.docx");

    let (_runtime, exported) = redline_from_pair(&before_path, &after_path);
    let xml = extract_document_xml(&exported);
    let root = parse_xml(&xml);

    // Collect move range markers
    let mut move_from_starts = Vec::new();
    find_all_elements(&root, "moveFromRangeStart", &mut move_from_starts);
    let mut move_from_ends = Vec::new();
    find_all_elements(&root, "moveFromRangeEnd", &mut move_from_ends);
    let mut move_to_starts = Vec::new();
    find_all_elements(&root, "moveToRangeStart", &mut move_to_starts);
    let mut move_to_ends = Vec::new();
    find_all_elements(&root, "moveToRangeEnd", &mut move_to_ends);

    assert!(
        !move_from_starts.is_empty(),
        "reordered paragraphs should produce moveFromRangeStart elements, \
         but none found. The diff engine may not be detecting moves. XML snippet: {}",
        &xml[..xml.len().min(2000)]
    );
    assert!(
        !move_from_ends.is_empty(),
        "reordered paragraphs should produce moveFromRangeEnd elements"
    );
    assert!(
        !move_to_starts.is_empty(),
        "reordered paragraphs should produce moveToRangeStart elements"
    );
    assert!(
        !move_to_ends.is_empty(),
        "reordered paragraphs should produce moveToRangeEnd elements"
    );

    // The move source and destination should be linked by matching w:name
    let from_names: HashSet<&str> = move_from_starts
        .iter()
        .filter_map(|el| attr_value(el, "w:name"))
        .collect();
    let to_names: HashSet<&str> = move_to_starts
        .iter()
        .filter_map(|el| attr_value(el, "w:name"))
        .collect();

    let common_names: HashSet<&&str> = from_names.intersection(&to_names).collect();
    assert!(
        !common_names.is_empty(),
        "moveFromRangeStart and moveToRangeStart should share at least one w:name. \
         From names: {:?}, To names: {:?}",
        from_names,
        to_names
    );

    // Verify that content uses w:moveFrom/w:moveTo (not plain w:del/w:ins)
    let mut move_froms = Vec::new();
    find_all_elements(&root, "moveFrom", &mut move_froms);
    let mut move_tos = Vec::new();
    find_all_elements(&root, "moveTo", &mut move_tos);

    assert!(
        !move_froms.is_empty(),
        "moved content should use w:moveFrom containers (not plain w:del)"
    );
    assert!(
        !move_tos.is_empty(),
        "moved content should use w:moveTo containers (not plain w:ins)"
    );
}

// ── Test 2: Move detection minimum text length ──────────────────────────

/// The move detection implementation requires >=20 characters for move detection.
/// Short paragraphs should NOT be detected as moves — they should be treated
/// as plain del+ins.
///
/// Old: "AB" / "CD"
/// New: "CD" / "AB"
#[test]
fn spec_move_detection_minimum_text_length() {
    let before_path = format!("{FIXTURES}/short-paragraphs/before.docx");
    let after_path = format!("{FIXTURES}/short-paragraphs/after.docx");

    let (_runtime, exported) = redline_from_pair(&before_path, &after_path);
    let xml = extract_document_xml(&exported);
    let root = parse_xml(&xml);

    // Should NOT have move range markers for short text
    let mut move_from_starts = Vec::new();
    find_all_elements(&root, "moveFromRangeStart", &mut move_from_starts);
    let mut move_to_starts = Vec::new();
    find_all_elements(&root, "moveToRangeStart", &mut move_to_starts);

    assert!(
        move_from_starts.is_empty(),
        "short text (<20 chars) should NOT produce moveFromRangeStart. \
         Move detection should only fire for substantial text. Found {} markers.",
        move_from_starts.len()
    );
    assert!(
        move_to_starts.is_empty(),
        "short text (<20 chars) should NOT produce moveToRangeStart. Found {} markers.",
        move_to_starts.len()
    );

    // Should NOT have moveFrom/moveTo content containers
    let mut move_froms = Vec::new();
    find_all_elements(&root, "moveFrom", &mut move_froms);
    let mut move_tos = Vec::new();
    find_all_elements(&root, "moveTo", &mut move_tos);

    assert!(
        move_froms.is_empty(),
        "short text should NOT produce w:moveFrom containers, got {}",
        move_froms.len()
    );
    assert!(
        move_tos.is_empty(),
        "short text should NOT produce w:moveTo containers, got {}",
        move_tos.len()
    );
}

// ── Test 3: Move ranges have matching IDs ────────────────────────────────

/// ECMA-376 §17.13.5.23-28: Every moveFromRangeStart must pair with
/// moveFromRangeEnd by id, and link to moveToRangeStart/End by name.
///
/// Uses the multi-paragraph-move scenario for robust testing.
#[test]
fn spec_move_ranges_have_matching_ids() {
    let before_path = format!("{FIXTURES}/multi-paragraph-move/before.docx");
    let after_path = format!("{FIXTURES}/multi-paragraph-move/after.docx");

    let (_runtime, exported) = redline_from_pair(&before_path, &after_path);
    let xml = extract_document_xml(&exported);
    let root = parse_xml(&xml);

    // Collect all move range markers
    let mut move_from_starts = Vec::new();
    find_all_elements(&root, "moveFromRangeStart", &mut move_from_starts);
    let mut move_from_ends = Vec::new();
    find_all_elements(&root, "moveFromRangeEnd", &mut move_from_ends);
    let mut move_to_starts = Vec::new();
    find_all_elements(&root, "moveToRangeStart", &mut move_to_starts);
    let mut move_to_ends = Vec::new();
    find_all_elements(&root, "moveToRangeEnd", &mut move_to_ends);

    // Must have at least one move range pair
    assert!(
        !move_from_starts.is_empty(),
        "multi-paragraph-move should produce moveFromRangeStart elements"
    );
    assert!(
        !move_to_starts.is_empty(),
        "multi-paragraph-move should produce moveToRangeStart elements"
    );

    // Every moveFromRangeStart must have a matching moveFromRangeEnd with the same id
    let from_start_ids: HashSet<&str> = move_from_starts
        .iter()
        .filter_map(|el| attr_value(el, "w:id"))
        .collect();
    let from_end_ids: HashSet<&str> = move_from_ends
        .iter()
        .filter_map(|el| attr_value(el, "w:id"))
        .collect();

    for id in &from_start_ids {
        assert!(
            from_end_ids.contains(id),
            "moveFromRangeStart with id={} has no matching moveFromRangeEnd. \
             Start IDs: {:?}, End IDs: {:?}",
            id,
            from_start_ids,
            from_end_ids
        );
    }

    // Every moveToRangeStart must have a matching moveToRangeEnd with the same id
    let to_start_ids: HashSet<&str> = move_to_starts
        .iter()
        .filter_map(|el| attr_value(el, "w:id"))
        .collect();
    let to_end_ids: HashSet<&str> = move_to_ends
        .iter()
        .filter_map(|el| attr_value(el, "w:id"))
        .collect();

    for id in &to_start_ids {
        assert!(
            to_end_ids.contains(id),
            "moveToRangeStart with id={} has no matching moveToRangeEnd. \
             Start IDs: {:?}, End IDs: {:?}",
            id,
            to_start_ids,
            to_end_ids
        );
    }

    // moveFromRangeStart and moveToRangeStart should be linked by w:name
    let from_names: HashMap<&str, &str> = move_from_starts
        .iter()
        .filter_map(|el| {
            let name = attr_value(el, "w:name")?;
            let id = attr_value(el, "w:id")?;
            Some((name, id))
        })
        .collect();
    let to_names: HashMap<&str, &str> = move_to_starts
        .iter()
        .filter_map(|el| {
            let name = attr_value(el, "w:name")?;
            let id = attr_value(el, "w:id")?;
            Some((name, id))
        })
        .collect();

    for name in from_names.keys() {
        assert!(
            to_names.contains_key(name),
            "moveFromRangeStart with name={} has no matching moveToRangeStart. \
             From names: {:?}, To names: {:?}",
            name,
            from_names.keys().collect::<Vec<_>>(),
            to_names.keys().collect::<Vec<_>>()
        );
    }
}

// ── Test 4: Import preserves pre-existing insertions ────────────────────

/// ISO 29500-1 §17.13.5.18: When importing a DOCX that already has tracked
/// insertions (w:ins), the canonical model should preserve them with correct
/// TrackingStatus::Inserted.
///
/// Fixture has: "Hello " + [inserted: "wonderful "] + "world."
#[test]
fn spec_import_preserves_preexisting_insertions() {
    let (_runtime, doc) = import_fixture_canonical("preexisting-insertions");

    let paras = common::all_paragraphs(&doc);
    assert!(!paras.is_empty(), "should have at least one paragraph");

    let p = &paras[0];
    let segments = &p.segments;

    // Should have at least one segment with Inserted status
    let inserted_segments: Vec<_> = segments
        .iter()
        .filter(|s| matches!(s.status, TrackingStatus::Inserted(_)))
        .collect();

    assert!(
        !inserted_segments.is_empty(),
        "should have at least one Inserted segment from w:ins markup. \
         Segment statuses: {:?}",
        segments
            .iter()
            .map(|s| format!("{:?}", s.status))
            .collect::<Vec<_>>()
    );

    // Verify the inserted text contains "wonderful"
    let inserted_text: String = inserted_segments
        .iter()
        .flat_map(|s| &s.inlines)
        .filter_map(|i| match i {
            InlineNode::Text(t) => Some(t.text.as_str()),
            _ => None,
        })
        .collect();

    assert!(
        inserted_text.contains("wonderful"),
        "inserted segment should contain 'wonderful', got: '{}'",
        inserted_text
    );

    // Verify RevisionInfo is preserved
    if let TrackingStatus::Inserted(rev) = &inserted_segments[0].status {
        assert_eq!(
            rev.author.as_deref(),
            Some("Insert Author"),
            "RevisionInfo author should be 'Insert Author'"
        );
        assert_eq!(
            rev.date.as_deref(),
            Some("2025-03-01T12:00:00Z"),
            "RevisionInfo date should be '2025-03-01T12:00:00Z'"
        );
    }
}

