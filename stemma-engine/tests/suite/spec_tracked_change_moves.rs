//! Spec-compliance tests: Move Tracking and Tracked Change Import Fidelity
//!
//! These tests verify that:
//! 1. The diff engine detects paragraph moves and produces moveFrom/moveTo
//!    markup (ECMA-376 §17.13.5.22-28) instead of plain del+ins.
//! 2. The move detection minimum text length (20 chars) is respected.
//! 3. Move range pairs are correctly linked by matching name attributes.
//! 4. Pre-existing tracked changes (w:ins, w:del) in imported DOCX are
//!    preserved in the canonical model with correct TrackingStatus and
//!    RevisionInfo.
//! 5. Accept/reject of moves keeps content in the correct location.
//!
//! Fixture DOCX files are generated by
//! `testdata/spec-compliance/tracked-change-moves/create_docs.py`.

