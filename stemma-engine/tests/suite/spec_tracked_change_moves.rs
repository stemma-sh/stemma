// ── Test 6: Accept moves keeps destination content ──────────────────────

/// ECMA-376 implied: Accepting a move keeps content at destination (w:moveTo
/// unwrapped), removes content at source (w:moveFrom dropped).
///
/// Uses the reordered-paragraphs fixture. After diff_and_redline, accept all
/// tracked changes at the DOCX XML level via normalize_docx (which handles
/// moveFrom/moveTo correctly), then re-import and verify paragraph order
/// matches the NEW (target) document order: Alpha, Charlie, Bravo.
#[test]
fn spec_accept_moves_keeps_destination_content() {
    let before_path = format!("{FIXTURES}/reordered-paragraphs/before.docx");
    let after_path = format!("{FIXTURES}/reordered-paragraphs/after.docx");

    let (_runtime, exported) = redline_from_pair(&before_path, &after_path);

    // Accept all tracked changes at the DOCX XML level.
    // normalize_docx handles moveFrom (drop) and moveTo (unwrap) correctly.
    let archive = DocxArchive::read(&exported).expect("parse redline DOCX");
    let (accepted_archive, _result) =
        stemma::normalize::normalize_docx(&archive).expect("normalize (accept all)");
    let accepted_bytes = accepted_archive.write().expect("write accepted DOCX");

    // Re-import the accepted DOCX
    let runtime2 = SimpleRuntime::new();
    let import = runtime2
        .import_docx(&accepted_bytes)
        .expect("re-import accepted");
    let accepted = import.canonical;

    // After accepting, content should be in the new (target) order:
    // Alpha, Charlie, Bravo
    let paras = common::all_paragraphs(&accepted);
    let texts: Vec<String> = paras.iter().map(|p| common::paragraph_text(p)).collect();

    // Filter to non-empty paragraphs (python-docx sometimes adds empty ones)
    let content_texts: Vec<&str> = texts
        .iter()
        .map(|t| t.trim())
        .filter(|t| !t.is_empty())
        .collect();

    assert!(
        content_texts.len() >= 3,
        "accepted document should have at least 3 non-empty paragraphs, got {}: {:?}",
        content_texts.len(),
        content_texts
    );

    // Charlie should appear before Bravo (the new order)
    let charlie_pos = content_texts
        .iter()
        .position(|t| t.contains("Charlie"))
        .expect("should find paragraph containing 'Charlie'");
    let bravo_pos = content_texts
        .iter()
        .position(|t| t.contains("Bravo"))
        .expect("should find paragraph containing 'Bravo'");

    assert!(
        charlie_pos < bravo_pos,
        "after accepting moves, Charlie (pos={}) should appear before Bravo (pos={}). \
         Accept should keep content at its destination (new) location. \
         Paragraphs: {:?}",
        charlie_pos,
        bravo_pos,
        content_texts
    );

    // Each paragraph should appear exactly once (move source was removed)
    let charlie_count = content_texts
        .iter()
        .filter(|t| t.contains("Charlie"))
        .count();
    let bravo_count = content_texts.iter().filter(|t| t.contains("Bravo")).count();
    assert_eq!(
        charlie_count, 1,
        "Charlie should appear exactly once after accept, got {}. \
         The moveFrom (source) copy should have been removed. Paragraphs: {:?}",
        charlie_count, content_texts
    );
    assert_eq!(
        bravo_count, 1,
        "Bravo should appear exactly once after accept, got {}. Paragraphs: {:?}",
        bravo_count, content_texts
    );
}

