//! Spec-compliance tests: Linked Style Resolution Edge Cases
//!
//! These tests target edge cases in linked style resolution (ISO 29500-1
//! §17.7.4.6) and default character style application (§17.7.4.17).
//!
//! Coverage:
//!   - Linked char basedOn chain properties cascade correctly (underline,
//!     color, highlight, italic) without overwriting the para style's
//!     explicit properties
//!   - Para style explicit properties preserved through linked overlay
//!   - Explicit char style on a linked para gets full resolved chain
//!   - Default character style applied to unstyled runs (§17.7.4.17)
//!   - Default char style + explicit char style merge correctly
//!   - Direct formatting overrides linked char style properties
//!   - Linked overlay does NOT leak into child styles via basedOn chain
//!   - Para basedOn chain with linked parent isolates link correctly
//!   - Linked char matching para values causes no information loss
//!
//! Fixture DOCX files generated by
//! `testdata/spec-compliance/style-cascade-linked-edge/create_docs.py`.

use stemma::{HighlightColor, InlineNode, Mark, MarkValue};

use crate::common;

const FIXTURES: &str = "testdata/spec-compliance/style-cascade-linked-edge";

// ── helpers ──────────────────────────────────────────────────────────────

fn text_nodes(p: &stemma::ParagraphNode) -> Vec<stemma::TextNode> {
    p.all_inlines_owned()
        .into_iter()
        .filter_map(|i| match i {
            InlineNode::Text(t) => Some(*t),
            _ => None,
        })
        .collect()
}

fn find_para_by_text<'a>(
    paras: &[&'a stemma::ParagraphNode],
    needle: &str,
) -> &'a stemma::ParagraphNode {
    paras
        .iter()
        .find(|p| common::paragraph_text(p).contains(needle))
        .unwrap_or_else(|| panic!("should find paragraph containing {:?}", needle))
}

