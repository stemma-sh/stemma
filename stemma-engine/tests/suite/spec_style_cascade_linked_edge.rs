// 1. Linked char basedOn chain property loss — overcorrection of Bug 2
//
// The fix for Bug 2 (use raw marks instead of resolved chain) prevents
// the linked char style's basedOn chain from overwriting the para style's
// explicit properties. But it also prevents the char style's basedOn chain
// from CONTRIBUTING properties that the para style doesn't set at all.
//
// Per §17.7.4.6, the linked char style provides the run-level rPr for
// the paragraph style. This should include the char style's full resolved
// basedOn chain — but only override properties that the para style hasn't
// explicitly set.
// ═══════════════════════════════════════════════════════════════════════════

/// §17.7.4.6: LinkedPara (sz=32, Arial) linked to LinkedChar (bold=on,
/// basedOn BaseChar(underline, color=0000FF, highlight=yellow)).
/// The para style should preserve sz=32 and font=Arial (its own properties).
/// Bold should come from LinkedChar's raw marks.
/// Underline, color, highlight should come from LinkedChar's basedOn chain.
#[test]
fn linked_based_on_loss_preserves_para_sz() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "linked-based-on-loss");
    let paras = common::all_paragraphs(&doc);

    let p = find_para_by_text(&paras, "Linked basedOn loss test");
    let nodes = text_nodes(p);
    let run = &nodes[0];

    // Para style explicitly sets sz=32 — should be preserved
    assert_eq!(
        run.style_props.font_size,
        Some(32),
        "LinkedPara sets sz=32 in rPr. The linked char overlay should NOT \
         overwrite this (LinkedChar doesn't explicitly set sz). Got {:?}",
        run.style_props.font_size
    );
}

