/// §17.7.4.6: MatchPara (bold, sz=28, italic) linked to MatchChar
/// (bold, sz=28, color=0000FF). Both set bold and sz=28. The char also
/// sets color. The para's italic should be preserved since the char
/// doesn't set it.
#[test]
fn linked_explicit_match_preserves_italic() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "linked-char-explicit-match");
    let paras = common::all_paragraphs(&doc);

    let p = find_para_by_text(&paras, "Linked match test");
    let nodes = text_nodes(p);
    let run = &nodes[0];

    assert!(
        run.marks.contains(&Mark::Bold),
        "Both para and char set bold=on."
    );
    assert_eq!(
        run.style_props.font_size,
        Some(28),
        "Both para and char set sz=28."
    );
    assert!(
        run.marks.contains(&Mark::Italic),
        "MatchPara sets italic=on. MatchChar doesn't set italic. \
         Para's italic should be preserved."
    );
    assert_eq!(
        run.style_props.color.as_deref(),
        Some("0000FF"),
        "MatchChar sets color=0000FF. Para doesn't set color. \
         Char's color should apply via linked overlay."
    );
}
