// ═══════════════════════════════════════════════════════════════════════════
// Tab stops through deep basedOn chain with clears (§17.3.1.38)
//
// This tests the overlay_tab_stops logic through a three-level style chain.
// The cascade-resolved values live in the DERIVED view field
// `effective_tab_stops_rel` (never serialized, never compared);
// `ParagraphNode.tab_stops` is authored-only (direct pPr w:tabs verbatim),
// so paragraphs whose tabs come entirely from the style chain carry an
// empty `tab_stops` and a populated effective view.
// ═══════════════════════════════════════════════════════════════════════════

/// §17.3.1.38: Grandparent defines 3 tabs, parent clears one and adds one,
/// child clears another and adds one. Verify the effective tab stops.
///
/// Reads `effective_tab_stops_rel` (the style-RESOLVED view value):
/// `tab_stops` now holds only the paragraph's own authored pPr stops
/// (authored/effective split), and these fixtures author none directly.
/// The fixtures carry no indent, so relative positions equal absolute.
#[test]
fn tab_stops_deep_chain_grandparent() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "tab-stops-deep-chain");
    let paras = common::all_paragraphs(&doc);

    // Para 0: GrandparentTabs — 720(left), 1440(center), 2880(right/dot)
    let p0 = find_para_by_text(&paras, "Grandparent tabs para");
    assert_eq!(
        p0.effective_tab_stops_rel.len(),
        3,
        "grandparent should have 3 tab stops, got {}: {:?}",
        p0.effective_tab_stops_rel.len(),
        p0.effective_tab_stops_rel
            .iter()
            .map(|t| t.position)
            .collect::<Vec<_>>()
    );
    assert_eq!(p0.effective_tab_stops_rel[0].position, 720);
    assert_eq!(p0.effective_tab_stops_rel[0].alignment, TabAlignment::Left);
    assert_eq!(p0.effective_tab_stops_rel[1].position, 1440);
    assert_eq!(
        p0.effective_tab_stops_rel[1].alignment,
        TabAlignment::Center
    );
    assert_eq!(p0.effective_tab_stops_rel[2].position, 2880);
    assert_eq!(p0.effective_tab_stops_rel[2].alignment, TabAlignment::Right);
    assert_eq!(p0.effective_tab_stops_rel[2].leader, Some(TabLeader::Dot));
}

