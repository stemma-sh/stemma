//! Spec-compliance tests: Paragraph property cascade edge cases
//!
//! These tests verify correct paragraph property resolution through the
//! style chain:
//!
//! - pageBreakBefore resolved through style chain (§17.3.1.23)
//! - Paragraph shading resolved through style chain (§17.3.1.31)
//! - keepNext/keepLines w:val="0" correctly parsed as false (§17.3.1.15, §17.3.1.14)
//! - pageBreakBefore w:val="0" correctly parsed as false (§17.3.1.23)
//! - pageBreakBefore inherited through deep basedOn chain (§17.3.1.23 + §17.7.4.3)
//! - Tab stops through deep basedOn chain with clears (§17.3.1.38)
//! - Borders through basedOn chain — whole-element replacement (§17.3.1.24)
//! - contextualSpacing val="0" overrides style's true (§17.3.1.9)
//!
//! Fixture DOCX files generated by
//! `testdata/spec-compliance/style-cascade-ppr-edge/create_docs.py`.

use stemma::{TabAlignment, TabLeader};

use crate::common;

const FIXTURES: &str = "testdata/spec-compliance/style-cascade-ppr-edge";

// ── helpers ──────────────────────────────────────────────────────────────

fn find_para_by_text<'a>(
    paras: &[&'a stemma::ParagraphNode],
    needle: &str,
) -> &'a stemma::ParagraphNode {
    paras
        .iter()
        .find(|p| common::paragraph_text(p).contains(needle))
        .unwrap_or_else(|| panic!("should find paragraph containing {:?}", needle))
}

// ═══════════════════════════════════════════════════════════════════════════
// pageBreakBefore from style chain (§17.3.1.23)
//
// Verifies that page_break_before is correctly resolved through the style's
// basedOn chain, not just from direct pPr.
// ═══════════════════════════════════════════════════════════════════════════

/// §17.3.1.23: Style "BreakBefore" defines pageBreakBefore.
/// A paragraph with no direct pPr but referencing that style should
/// inherit page_break_before=true.
#[test]
fn page_break_before_inherited_from_style() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "page-break-before-from-style");
    let paras = common::all_paragraphs(&doc);

    // Para 0: no style — should be false
    let p0 = find_para_by_text(&paras, "No style no break");
    assert!(
        !p0.page_break_before,
        "para 0 (no style) should have page_break_before=false"
    );

    // Para 1: BreakBefore style — should inherit page_break_before=true
    let p1 = find_para_by_text(&paras, "Break style applied");
    assert!(
        p1.page_break_before,
        "para 1 (BreakBefore style) should inherit page_break_before=true from style \
         per §17.3.1.23."
    );
}

