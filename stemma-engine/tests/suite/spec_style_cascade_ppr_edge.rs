// Borders through basedOn chain (§17.3.1.24)
//
// Tests whole-element replacement: if a child style has its own pBdr,
// it replaces the parent's pBdr entirely. If a child has no pBdr,
// it inherits the parent's.
// ═══════════════════════════════════════════════════════════════════════════

/// §17.3.1.24: Parent style has top+bottom borders. A paragraph using the
/// parent style should have both.
#[test]
fn borders_parent_has_top_and_bottom() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "borders-based-on-chain");
    let paras = common::all_paragraphs(&doc);

    let p0 = find_para_by_text(&paras, "Parent borders top bottom");
    let borders = p0
        .borders
        .as_ref()
        .expect("para 0 (BorderParent style) should have borders");
    assert!(borders.top.is_some(), "parent style should have top border");
    assert!(
        borders.bottom.is_some(),
        "parent style should have bottom border"
    );
    assert_eq!(
        borders.top.as_ref().unwrap().color.as_deref(),
        Some("FF0000"),
        "top border color should be FF0000"
    );
    assert_eq!(
        borders.bottom.as_ref().unwrap().color.as_deref(),
        Some("0000FF"),
        "bottom border color should be 0000FF"
    );
}

/// §17.3.1.24: Child style (basedOn parent) defines only left border.
/// Per OOXML, pBdr is whole-element replacement — child's pBdr replaces
/// parent's entirely. Child should have only left, NOT top+bottom.
#[test]
fn borders_child_replaces_parent_entirely() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "borders-based-on-chain");
    let paras = common::all_paragraphs(&doc);

    let p1 = find_para_by_text(&paras, "Child border left only");
    let borders = p1
        .borders
        .as_ref()
        .expect("para 1 (BorderChild style) should have borders");

    // Child defines only left border — parent's top and bottom should NOT leak through
    assert!(
        borders.left.is_some(),
        "child style should have left border"
    );
    assert_eq!(
        borders.left.as_ref().unwrap().color.as_deref(),
        Some("00FF00"),
        "left border color should be 00FF00"
    );
    assert!(
        borders.top.is_none(),
        "child style should NOT have top border (pBdr is whole-element replacement, \
         child's pBdr replaces parent's entirely per §17.3.1.24)"
    );
    assert!(
        borders.bottom.is_none(),
        "child style should NOT have bottom border (pBdr is whole-element replacement)"
    );
}

