// Test 5: Complete rPr element ordering
// ECMA-376 Annex A CT_RPr
// =========================================================================

/// ECMA-376 Annex A CT_RPr: All child elements of rPr must be in the correct
/// sequence order.
///
/// Expected order (positions from the schema):
///   rStyle(1), rFonts(2), b(3), bCs(4), i(5), iCs(6), caps(7), smallCaps(8),
///   strike(9), dstrike(10), outline(11), shadow(12), emboss(13), imprint(14),
///   ..., vanish(17), ..., color(19), spacing(20), w(21), kern(22),
///   position(23), sz(24), szCs(25), highlight(26), u(27), bdr(28), ...,
///   vertAlign(31), rtl(32), cs(33), ..., lang(35), ..., rPrChange(40)
///
/// This test verifies that all emitted elements appear in non-decreasing
/// position order. The fixture has a run with b, i, rFonts, color, sz, szCs,
/// highlight, u, vertAlign, rtl, cs, lang.
///
/// This test catches any ordering violation in the serializer's output.
/// Note: the fixture does not include bdr, which is tested separately in
/// `spec_rpr_bdr_element_ordered_correctly`.
#[test]
fn spec_rpr_element_ordering_complete() {
    let before_path = format!("{FIXTURES}/rpr-full-ordering/before.docx");
    let after_path = format!("{FIXTURES}/rpr-full-ordering/after.docx");
    let exported = redline_export(&before_path, &after_path);
    let xml = extract_document_xml(&exported);
    let root = parse_xml(&xml);

    // ECMA-376 Annex A CT_RPr element ordering (position number -> element name)
    let ordering: &[(&str, u32)] = &[
        ("rStyle", 1),
        ("rFonts", 2),
        ("b", 3),
        ("bCs", 4),
        ("i", 5),
        ("iCs", 6),
        ("caps", 7),
        ("smallCaps", 8),
        ("strike", 9),
        ("dstrike", 10),
        ("outline", 11),
        ("shadow", 12),
        ("emboss", 13),
        ("imprint", 14),
        ("noProof", 15),
        ("snapToGrid", 16),
        ("vanish", 17),
        ("webHidden", 18),
        ("color", 19),
        ("spacing", 20),
        ("w", 21),
        ("kern", 22),
        ("position", 23),
        ("sz", 24),
        ("szCs", 25),
        ("highlight", 26),
        ("u", 27),
        ("effect", 28),
        ("bdr", 29),
        ("shd", 30),
        ("fitText", 31),
        ("vertAlign", 32),
        ("rtl", 33),
        ("cs", 34),
        ("em", 35),
        ("lang", 36),
        ("eastAsianLayout", 37),
        ("specVanish", 38),
        ("oMath", 39),
        ("rPrChange", 40),
    ];

    fn position_of(name: &str, ordering: &[(&str, u32)]) -> Option<u32> {
        ordering.iter().find(|(n, _)| *n == name).map(|(_, p)| *p)
    }

    // Find all rPr elements
    let mut rpr_elements = Vec::new();
    find_all_w_elements(&root, "rPr", &mut rpr_elements);

    // Find the rPr with the most known children (our test run)
    let rpr = rpr_elements
        .iter()
        .max_by_key(|rpr| {
            child_element_names(rpr)
                .iter()
                .filter(|n| position_of(n, ordering).is_some())
                .count()
        })
        .expect("exported DOCX should contain at least one w:rPr element");

    let child_names = child_element_names(rpr);

    // Sanity check: the rPr should have a meaningful number of elements
    let known_count = child_names
        .iter()
        .filter(|n| position_of(n, ordering).is_some())
        .count();
    assert!(
        known_count >= 5,
        "expected the test rPr to have at least 5 known elements, got {known_count}. \
         Child names: {child_names:?}"
    );

    // Verify all elements appear in non-decreasing position order
    let mut last_pos: u32 = 0;
    let mut last_name = String::new();

    for name in &child_names {
        if let Some(pos) = position_of(name, ordering) {
            assert!(
                pos >= last_pos,
                "ECMA-376 Annex A CT_RPr ordering violation: \
                 w:{name} (position {pos}) appears after w:{last_name} (position {last_pos}). \
                 Elements must be in ascending position order. \
                 Full rPr child order: {child_names:?}"
            );
            last_pos = pos;
            last_name = name.clone();
        }
        // Elements not in our ordering table are ignored (e.g., extension elements)
    }
}

