/// ISO 29500-1 §17.3.2.26: Explicit font names (w:ascii, w:hAnsi) should
/// survive import → diff_and_redline → export.
///
/// Unlike theme fonts, explicit font names are stored in `StyleProps.font_family`
/// and serialized back as `w:ascii`/`w:hAnsi` attributes by `build_rpr`.
#[test]
fn spec_explicit_font_survives_roundtrip() {
    let before_path = format!("{FIXTURES}/explicit-font/before.docx");
    let after_path = format!("{FIXTURES}/explicit-font/after.docx");
    let exported = redline_export(&before_path, &after_path);
    let xml = extract_document_xml(&exported);
    let root = parse_xml(&xml);

    let mut rfonts_elements = Vec::new();
    find_all_w_elements(&root, "rFonts", &mut rfonts_elements);

    // Find an rFonts with ascii="Courier New"
    let has_courier = rfonts_elements.iter().any(|el| {
        attr_value(el, "w:ascii")
            .or_else(|| attr_value(el, "ascii"))
            .is_some_and(|v| v == "Courier New")
    });

    assert!(
        has_courier,
        "ISO 29500-1 §17.3.2.26: rFonts with w:ascii='Courier New' should survive \
         roundtrip through the serializer. Found rFonts attributes: {:?}",
        rfonts_attr_summary(&rfonts_elements)
    );

    // Also verify hAnsi matches
    let has_hansi_courier = rfonts_elements.iter().any(|el| {
        attr_value(el, "w:hAnsi")
            .or_else(|| attr_value(el, "hAnsi"))
            .is_some_and(|v| v == "Courier New")
    });

    assert!(
        has_hansi_courier,
        "ISO 29500-1 §17.3.2.26: rFonts with w:hAnsi='Courier New' should survive roundtrip. \
         Found rFonts attributes: {:?}",
        rfonts_attr_summary(&rfonts_elements)
    );
}

// =========================================================================
// Test 4: bdr element ordered correctly in rPr
// ECMA-376 Annex A CT_RPr
// =========================================================================

/// Verifies that w:bdr (run border) is emitted at sequence position 29
/// per ECMA-376 Annex A CT_RPr, between w:u (27) and w:vertAlign (32).
#[test]

fn spec_rpr_bdr_element_ordered_correctly() {
    let before_path = format!("{FIXTURES}/rpr-bdr-ordering/before.docx");
    let after_path = format!("{FIXTURES}/rpr-bdr-ordering/after.docx");
    let exported = redline_export(&before_path, &after_path);
    let xml = extract_document_xml(&exported);
    let root = parse_xml(&xml);

    // Find rPr elements that contain bdr
    let mut rpr_elements = Vec::new();
    find_all_w_elements(&root, "rPr", &mut rpr_elements);

    let rpr_with_bdr: Vec<_> = rpr_elements
        .iter()
        .filter(|rpr| {
            rpr.children.iter().any(|c| match c {
                XMLNode::Element(el) => is_w_tag(el, "bdr"),
                _ => false,
            })
        })
        .collect();

    assert!(
        !rpr_with_bdr.is_empty(),
        "exported DOCX should contain at least one w:rPr with a w:bdr child \
         (the fixture has a run with a border). \
         All rPr child names: {:?}",
        rpr_elements
            .iter()
            .map(|rpr| child_element_names(rpr))
            .collect::<Vec<_>>()
    );

    for rpr in &rpr_with_bdr {
        let child_names = child_element_names(rpr);

        let bdr_pos = child_names.iter().position(|n| n == "bdr");
        let vert_align_pos = child_names.iter().position(|n| n == "vertAlign");
        let u_pos = child_names.iter().position(|n| n == "u");
        let highlight_pos = child_names.iter().position(|n| n == "highlight");

        // bdr must come BEFORE vertAlign (if both present)
        if let (Some(bdr), Some(va)) = (bdr_pos, vert_align_pos) {
            assert!(
                bdr < va,
                "ECMA-376 Annex A CT_RPr: w:bdr (pos 28) must come before w:vertAlign (pos 31), \
                 but bdr is at index {bdr} and vertAlign is at index {va} in rPr children. \
                 Actual order: {child_names:?}"
            );
        }

        // bdr must come AFTER u (if both present)
        if let (Some(u), Some(bdr)) = (u_pos, bdr_pos) {
            assert!(
                u < bdr,
                "ECMA-376 Annex A CT_RPr: w:u (pos 27) must come before w:bdr (pos 28), \
                 but u is at index {u} and bdr is at index {bdr} in rPr children. \
                 Actual order: {child_names:?}"
            );
        }

        // bdr must come AFTER highlight (if both present)
        if let (Some(hl), Some(bdr)) = (highlight_pos, bdr_pos) {
            assert!(
                hl < bdr,
                "ECMA-376 Annex A CT_RPr: w:highlight (pos 26) must come before w:bdr (pos 28), \
                 but highlight is at index {hl} and bdr is at index {bdr}. \
                 Actual order: {child_names:?}"
            );
        }
    }
}

