// Test 6: w:webHidden survives the authoring (model-rebuild) path
// ISO/IEC 29500-1 §17.3.2.44
// =========================================================================

/// Build a minimal in-memory `.docx` wrapping `body_xml` in
/// `<w:document><w:body>…</w:body></w:document>`.
fn make_docx(body_xml: &str) -> Vec<u8> {
    use std::io::Write;
    use zip::write::FileOptions;

    let document_xml = format!(
        r#"<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<w:document xmlns:w="http://schemas.openxmlformats.org/wordprocessingml/2006/main"><w:body>{body_xml}</w:body></w:document>"#
    );
    let content_types = r#"<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types"><Default Extension="rels" ContentType="application/vnd.openxmlformats-package.relationships+xml"/><Default Extension="xml" ContentType="application/xml"/><Override PartName="/word/document.xml" ContentType="application/vnd.openxmlformats-officedocument.wordprocessingml.document.main+xml"/></Types>"#;
    let rels = r#"<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships"><Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument" Target="word/document.xml"/></Relationships>"#;
    let doc_rels = r#"<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships"/>"#;

    let mut buf = Vec::new();
    {
        let mut zip = zip::ZipWriter::new(Cursor::new(&mut buf));
        let opts: FileOptions = FileOptions::default();
        zip.start_file("[Content_Types].xml", opts).unwrap();
        zip.write_all(content_types.as_bytes()).unwrap();
        zip.start_file("_rels/.rels", opts).unwrap();
        zip.write_all(rels.as_bytes()).unwrap();
        zip.start_file("word/_rels/document.xml.rels", opts)
            .unwrap();
        zip.write_all(doc_rels.as_bytes()).unwrap();
        zip.start_file("word/document.xml", opts).unwrap();
        zip.write_all(document_xml.as_bytes()).unwrap();
        zip.finish().unwrap();
    }
    buf
}

/// Diff-and-redline an in-memory before/after pair, export, return DOCX bytes.
/// Mirrors `redline_export` but takes bytes so the test is self-contained.
fn redline_export_bytes(before: &[u8], after: &[u8]) -> Vec<u8> {
    let runtime = SimpleRuntime::new();
    let ib = runtime.import_docx(before).expect("import before");
    let ia = runtime.import_docx(after).expect("import after");
    let meta = TransactionMeta {
        author: "spec_run_serialization".to_string(),
        reason: Some("webHidden authoring-path spec test".to_string()),
        timestamp_utc: Some("2025-01-15T10:30:00Z".to_string()),
    };
    let apply = runtime
        .diff_and_redline(&ib.doc_handle, &ia.doc_handle, meta)
        .expect("diff_and_redline");
    assert!(apply.applied, "redline should be applied");
    runtime
        .export_docx(&ib.doc_handle, ExportMode::Redline)
        .expect("export redline")
}

/// ISO/IEC 29500-1 §17.3.2.44: `w:webHidden` is a CT_OnOff run property that
/// hides a run when the document is displayed as a web page. It is a real
/// formatting property, not packaging noise, so it must survive an edit that
/// rebuilds the run from the domain model — not merely the verbatim-bytes path.
///
/// The before document carries a run with `<w:webHidden/>` in a paragraph that
/// is left untouched; the edit changes the text of a *different* paragraph.
/// `diff_and_redline` + export forces the whole body through
/// `serialize_canonical_docx` → `build_rpr`, so a model that dropped webHidden
/// (verbatim-passthrough only) would lose it here. This is the authoring-path
/// regression guard for the webHidden model.
#[test]
fn spec_web_hidden_survives_authoring_rebuild() {
    let before = make_docx(
        r#"<w:p><w:r><w:rPr><w:webHidden/></w:rPr><w:t>hidden on web</w:t></w:r></w:p>
<w:p><w:r><w:t>edit me</w:t></w:r></w:p><w:sectPr/>"#,
    );
    let after = make_docx(
        r#"<w:p><w:r><w:rPr><w:webHidden/></w:rPr><w:t>hidden on web</w:t></w:r></w:p>
<w:p><w:r><w:t>edited!</w:t></w:r></w:p><w:sectPr/>"#,
    );

    let exported = redline_export_bytes(&before, &after);
    let xml = extract_document_xml(&exported);
    let root = parse_xml(&xml);

    // The webHidden run was rebuilt by the serializer; webHidden must remain.
    let mut rpr_elements = Vec::new();
    find_all_w_elements(&root, "rPr", &mut rpr_elements);
    let has_web_hidden = rpr_elements.iter().any(|rpr| {
        rpr.children.iter().any(|c| match c {
            XMLNode::Element(el) => is_w_tag(el, "webHidden"),
            _ => false,
        })
    });

    assert!(
        has_web_hidden,
        "ISO/IEC 29500-1 §17.3.2.44: w:webHidden must survive an edit that \
         rebuilds the run through build_rpr, but no w:webHidden child was found. \
         All rPr child names: {:?}",
        rpr_elements
            .iter()
            .map(|rpr| child_element_names(rpr))
            .collect::<Vec<_>>()
    );
}
