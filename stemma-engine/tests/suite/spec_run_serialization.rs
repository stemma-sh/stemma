//! Spec-compliance tests: Run Serialization (theme fonts & rPr element ordering)
//!
//! These tests verify two categories of serialization correctness:
//! 1. Theme font roundtrip fidelity — do theme font references (asciiTheme,
//!    hAnsiTheme, eastAsiaTheme) survive import → diff_and_redline → export?
//! 2. rPr element ordering — does the serializer emit child elements of
//!    w:rPr in the order mandated by ECMA-376 Annex A (CT_RPr)?
//!
//! Each fixture is a before/after DOCX pair with a text change in a separate
//! paragraph. This forces the code through `serialize_canonical_docx` (which
//! calls `build_rpr`). An identity diff takes an early-return path that
//! preserves the original XML bytes and would bypass the serializer entirely.
//!
//! Fixture DOCX files are generated by
//! `testdata/spec-compliance/run-serialization/create_docs.py`.

use std::fs;
use std::io::{Cursor, Read};

use stemma::{DocxRuntime, ExportMode, SimpleRuntime, TransactionMeta};
use xmltree::{Element, XMLNode};
use zip::ZipArchive;

const FIXTURES: &str = "testdata/spec-compliance/run-serialization";

// ── helpers ──────────────────────────────────────────────────────────────

/// Diff-and-redline a before/after pair, export, return raw DOCX bytes.
///
/// The text change between before and after forces the code through
/// `serialize_canonical_docx`, which rebuilds the XML from the domain model.
fn redline_export(before_path: &str, after_path: &str) -> Vec<u8> {
    let before = fs::read(before_path).unwrap_or_else(|e| panic!("read {before_path}: {e}"));
    let after = fs::read(after_path).unwrap_or_else(|e| panic!("read {after_path}: {e}"));

    let runtime = SimpleRuntime::new();
    let ib = runtime.import_docx(&before).expect("import before");
    let ia = runtime.import_docx(&after).expect("import after");

    let meta = TransactionMeta {
        author: "spec_run_serialization".to_string(),
        reason: Some("run-serialization spec test".to_string()),
        timestamp_utc: Some("2025-01-15T10:30:00Z".to_string()),
    };
    let apply = runtime
        .diff_and_redline(&ib.doc_handle, &ia.doc_handle, meta)
        .expect("diff_and_redline");
    assert!(apply.applied, "redline should be applied");

    runtime
        .export_docx(&ib.doc_handle, ExportMode::Redline)
        .expect("export redline")
}

/// Extract word/document.xml from a DOCX byte buffer.
fn extract_document_xml(docx_bytes: &[u8]) -> String {
    let cursor = Cursor::new(docx_bytes);
    let mut zip = ZipArchive::new(cursor).expect("open DOCX zip");
    let mut file = zip
        .by_name("word/document.xml")
        .expect("DOCX must contain word/document.xml");
    let mut out = String::new();
    file.read_to_string(&mut out)
        .expect("read word/document.xml");
    out
}

fn parse_xml(xml: &str) -> Element {
    Element::parse(Cursor::new(xml.as_bytes())).expect("parse XML")
}

fn local_name(name: &str) -> &str {
    match name.rsplit_once(':') {
        Some((_, local)) => local,
        None => name,
    }
}

const WORD_NS: &str = "http://schemas.openxmlformats.org/wordprocessingml/2006/main";

fn is_w_tag(element: &Element, local: &str) -> bool {
    let name = local_name(&element.name);
    if name != local {
        return false;
    }
    if element.prefix.as_deref() == Some("w") {
        return true;
    }
    if element.namespace.as_deref() == Some(WORD_NS) {
        return true;
    }
    element.name == local || element.name == format!("w:{local}")
}

fn attr_value<'a>(element: &'a Element, qname: &str) -> Option<&'a str> {
    let (_prefix, local) = match qname.split_once(':') {
        Some((p, l)) if !p.is_empty() && !l.is_empty() => (Some(p), l),
        _ => (None, qname),
    };

    for (name, value) in &element.attributes {
        if name.local_name == local {
            return Some(value);
        }
    }

    for (name, value) in &element.attributes {
        if name.local_name == qname {
            return Some(value);
        }
    }

    None
}

/// Find all elements matching a w: tag, recursively.
fn find_all_w_elements<'a>(root: &'a Element, tag: &str, out: &mut Vec<&'a Element>) {
    if is_w_tag(root, tag) {
        out.push(root);
    }
    for child in &root.children {
        if let XMLNode::Element(el) = child {
            find_all_w_elements(el, tag, out);
        }
    }
}

/// Find a direct w: child element with a given tag.
#[allow(dead_code)]
fn find_w_child<'a>(parent: &'a Element, tag: &str) -> Option<&'a Element> {
    parent.children.iter().find_map(|child| match child {
        XMLNode::Element(el) if is_w_tag(el, tag) => Some(el),
        _ => None,
    })
}

/// Get all direct child element local names (w: namespace) of an element, in order.
fn child_element_names(parent: &Element) -> Vec<String> {
    parent
        .children
        .iter()
        .filter_map(|child| match child {
            XMLNode::Element(el) => Some(local_name(&el.name).to_string()),
            _ => None,
        })
        .collect()
}

/// Format rFonts attributes for diagnostic output.
fn rfonts_attr_summary(elements: &[&Element]) -> Vec<Vec<String>> {
    elements
        .iter()
        .map(|el| {
            el.attributes
                .iter()
                .map(|(k, v)| format!("{}={}", k.local_name, v))
                .collect::<Vec<_>>()
        })
        .collect()
}

// =========================================================================
// Test 1: Theme font reference preserved on roundtrip (asciiTheme)
// ISO 29500-1 §17.3.2.26
// =========================================================================

/// ISO 29500-1 §17.3.2.26: Theme font references (asciiTheme/hAnsiTheme)
/// should survive import → diff_and_redline → export.
///
/// `StyleProps` carries theme font reference strings alongside resolved font
/// names. On export, `build_rpr` emits both the theme attribute (for
/// theme-aware readers) and the direct attribute (as fallback).
#[test]
fn spec_theme_font_reference_preserved_on_roundtrip() {
    let before_path = format!("{FIXTURES}/theme-font-ascii/before.docx");
    let after_path = format!("{FIXTURES}/theme-font-ascii/after.docx");
    let exported = redline_export(&before_path, &after_path);
    let xml = extract_document_xml(&exported);
    let root = parse_xml(&xml);

    // Find all rFonts elements in the output
    let mut rfonts_elements = Vec::new();
    find_all_w_elements(&root, "rFonts", &mut rfonts_elements);

    // There should be at least one rFonts (our run's font reference)
    assert!(
        !rfonts_elements.is_empty(),
        "exported DOCX should contain at least one w:rFonts element"
    );

    // Check if any rFonts has asciiTheme or hAnsiTheme attributes
    let has_ascii_theme = rfonts_elements
        .iter()
        .any(|el| attr_value(el, "w:asciiTheme").is_some());
    let has_hansi_theme = rfonts_elements
        .iter()
        .any(|el| attr_value(el, "w:hAnsiTheme").is_some());

    assert!(
        has_ascii_theme,
        "ISO 29500-1 §17.3.2.26: rFonts should preserve w:asciiTheme='minorHAnsi' \
         on roundtrip, but no rFonts element has an asciiTheme attribute. \
         The theme reference was resolved to an explicit font name and lost. \
         Found rFonts attributes: {:?}",
        rfonts_attr_summary(&rfonts_elements)
    );
    assert!(
        has_hansi_theme,
        "ISO 29500-1 §17.3.2.26: rFonts should preserve w:hAnsiTheme='minorHAnsi' \
         on roundtrip, but no rFonts element has an hAnsiTheme attribute. \
         Found rFonts attributes: {:?}",
        rfonts_attr_summary(&rfonts_elements)
    );
}

