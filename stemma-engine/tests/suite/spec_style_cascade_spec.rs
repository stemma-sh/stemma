// 2. lineRule absent defaults to "auto" (§17.3.1.33)
//
// §17.3.1.33: "If [lineRule] is omitted, then it shall be assumed to be
// of a value auto if a line attribute value is present."
//
// This applies to style definitions too. When a style's w:spacing sets
// w:line but omits lineRule, the effective lineRule should be "auto".
// And when a child style's w:spacing overrides line but omits lineRule,
// the parent's lineRule should NOT be inherited — it resets to "auto".
// ═══════════════════════════════════════════════════════════════════════════

/// §17.3.1.33: Style with line=360 and no lineRule → lineRule=auto.
#[test]
fn line_rule_absent_defaults_auto() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "line-rule-absent");
    let paras = common::all_paragraphs(&doc);

    let p = paras
        .iter()
        .find(|p| common::paragraph_text(p).contains("No lineRule style text"))
        .expect("should find StyleNoLineRule paragraph");

    let spacing = p
        .spacing
        .as_ref()
        .expect("para should have spacing from StyleNoLineRule");
    assert_eq!(
        spacing.line,
        Some(360),
        "line should be 360 from StyleNoLineRule"
    );
    assert_eq!(
        spacing.line_rule,
        Some(LineSpacingRule::Auto),
        "lineRule should be Auto when omitted but line is present (§17.3.1.33)"
    );
}

