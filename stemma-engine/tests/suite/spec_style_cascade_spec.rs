// 6. Mixed toggle properties across hierarchy levels
//    (§17.7.3 + MS-OI29500 §2.1.258)
//
// Each toggle property should be resolved independently. When different
// properties have different on/off patterns across paragraph and character
// style levels, they should not interfere with each other.
// ═══════════════════════════════════════════════════════════════════════════

/// §17.7.3: MixedPara (bold=on, italic=off, caps=on) + MixedChar (bold=off, italic=on).
/// Per MS-OI29500 §2.1.258 (reset semantics):
/// - bold: char=off overrides para=on → OFF
/// - italic: char=on overrides para=off → ON
/// - caps: only para sets it, char doesn't touch it → ON
#[test]
fn toggle_mixed_both_styles() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "toggle-mixed-across-levels");
    let paras = common::all_paragraphs(&doc);

    let p = paras
        .iter()
        .find(|p| common::paragraph_text(p).contains("Mixed toggle patterns"))
        .expect("should find mixed toggle paragraph");

    let nodes = text_nodes(p);
    let run = &nodes[0];

    // Bold: char style says off → overrides para style's on.
    assert!(
        !run.marks.contains(&Mark::Bold),
        "MixedChar bold=off should override MixedPara bold=on. \
         Per MS-OI29500 §2.1.258, char style resets the toggle value."
    );

    // Italic: char style says on → overrides para style's off.
    assert!(
        run.marks.contains(&Mark::Italic),
        "MixedChar italic=on should override MixedPara italic=off. \
         Per MS-OI29500 §2.1.258, char style resets the toggle value."
    );

    // Caps: only para style sets it, char style doesn't touch it.
    // The para style's caps=on should persist.
    assert!(
        run.style_props.caps == MarkValue::On,
        "MixedPara caps=on with MixedChar not setting caps → caps should be ON. \
         Per §17.7.3, unset properties at a higher level inherit from lower levels."
    );
}

