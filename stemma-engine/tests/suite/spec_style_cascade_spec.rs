// 3. Table conditional formatting corner cell precedence (§17.7.6)
//
// MS-OI29500 §17.4.54(a): When a cell is in both firstRow and firstCol,
// the NwCell conditional (if defined) has highest precedence.
// When NwCell is not defined, firstRow > firstCol per §17.7.6(c).
// ═══════════════════════════════════════════════════════════════════════════

/// §17.7.6: Table with NwCell conditional — cell(0,0) should get NwCell shading.
#[test]
fn table_conditional_corner_nw_cell_wins() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "table-conditional-corner");
    let tables = all_tables(&doc);
    assert!(
        tables.len() >= 2,
        "should have at least 2 tables, got {}",
        tables.len()
    );

    // Table 1: CornerTestWithNw style
    let table1 = tables[0];

    // Cell(0,0): NwCell should win (green = "00FF00")
    let cell_0_0 = &table1.rows[0].cells[0];
    let shading = cell_0_0
        .formatting
        .shading
        .as_ref()
        .expect("cell(0,0) should have shading from NwCell conditional");
    assert_eq!(
        shading.fill.as_deref(),
        Some("00FF00"),
        "cell(0,0) should have green fill from NwCell conditional (highest precedence). \
         Per MS-OI29500 §17.4.54(a), corner cells override firstRow/firstCol."
    );
}

