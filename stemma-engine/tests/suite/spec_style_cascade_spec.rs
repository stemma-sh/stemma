//! Spec-compliance tests: Style Cascade (ISO 29500-1 §17.7)
//!
//! These tests verify style cascade behavior against the OOXML specification,
//! targeting scenarios likely to expose implementation gaps:
//!
//! - §17.7.4.6 + §17.7.4.3: Linked styles with character style basedOn chains
//! - §17.3.1.33: lineRule defaults to "auto" when omitted
//! - §17.7.6: Table conditional formatting corner cell precedence
//! - §17.7.4.6: Linked style double-apply (implicit vs explicit)
//! - §17.7.3 + §17.7.4.3: Toggle explicit off vs inherit in basedOn chain
//! - §17.7.3 + MS-OI29500 §2.1.258: Mixed toggles across hierarchy levels
//!
//! Fixture DOCX files are generated by
//! `testdata/spec-compliance/style-cascade-spec/create_docs.py`.

#![allow(non_snake_case)]

use stemma::{BlockNode, InlineNode, LineSpacingRule, Mark, MarkValue};

use crate::common;

const FIXTURES: &str = "testdata/spec-compliance/style-cascade-spec";

// ── helpers ──────────────────────────────────────────────────────────────

fn text_nodes(p: &stemma::ParagraphNode) -> Vec<stemma::TextNode> {
    p.all_inlines_owned()
        .into_iter()
        .filter_map(|i| match i {
            InlineNode::Text(t) => Some(*t),
            _ => None,
        })
        .collect()
}

#[allow(dead_code)]
fn first_table(doc: &stemma::CanonDoc) -> &stemma::TableNode {
    doc.blocks
        .iter()
        .find_map(|b| match &b.block {
            BlockNode::Table(t) => Some(t.as_ref()),
            _ => None,
        })
        .expect("document should contain at least one table")
}

fn all_tables(doc: &stemma::CanonDoc) -> Vec<&stemma::TableNode> {
    doc.blocks
        .iter()
        .filter_map(|b| match &b.block {
            BlockNode::Table(t) => Some(t.as_ref()),
            _ => None,
        })
        .collect()
}

