// 1. Linked style with char style basedOn chain (§17.7.4.6 + §17.7.4.3)
//
// When a paragraph style P has w:link to character style C, and C has
// basedOn pointing to CharBase, the code overlays only C's raw marks
// (not its resolved basedOn chain) onto P's resolved chain. Properties
// that C inherits from CharBase (but doesn't set itself) will be lost.
// ═══════════════════════════════════════════════════════════════════════════

/// §17.7.4.6 + §17.7.4.3: LinkedPara links to LinkedChar which basedOn CharBase.
/// CharBase has italic=on and color=FF0000. LinkedChar only sets bold=on.
/// Runs in LinkedPara should get italic=on from CharBase (via LinkedChar's chain).
///
/// Bug hypothesis: overlay_marks uses linked_raw.marks (LinkedChar's own
/// raw marks = bold only). Italic from CharBase is NOT in linked_raw.marks,
/// so it gets lost. The run should be italic but likely isn't.
#[test]
fn linked_char_basedOn_italic_inherited() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "linked-char-basedOn");
    let paras = common::all_paragraphs(&doc);

    let p = paras
        .iter()
        .find(|p| common::paragraph_text(p).contains("Linked para with char basedOn chain"))
        .expect("should find linked para paragraph");

    let nodes = text_nodes(p);
    let run = &nodes[0];

    // Bold should be ON — LinkedChar sets it directly.
    assert!(
        run.marks.contains(&Mark::Bold),
        "LinkedPara run should be bold — LinkedChar sets bold=on directly."
    );

    // Italic should be ON — CharBase sets it, LinkedChar inherits via basedOn.
    // Bug: overlay_marks only uses LinkedChar's raw marks (just bold),
    // so italic from CharBase is lost when computing the paragraph style's
    // resolved marks.
    assert!(
        run.marks.contains(&Mark::Italic),
        "LinkedPara run should be italic — CharBase sets italic=on, \
         LinkedChar inherits it via basedOn chain (§17.7.4.3). \
         Bug: overlay_marks uses linked_raw.marks which only has bold, \
         so italic from the char style's basedOn parent is dropped."
    );
}

