/// §17.7.4.6: Explicit rStyle=LinkedChar2 run should produce the same
/// formatting as the implicit linked resolution.
#[test]
fn linked_double_apply_explicit_matches_implicit() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "linked-double-apply");
    let paras = common::all_paragraphs(&doc);

    let p = paras
        .iter()
        .find(|p| {
            let text = common::paragraph_text(p);
            text.contains("Implicit linked run") && text.contains("Explicit linked char run")
        })
        .expect("should find paragraph with both runs");

    let nodes = text_nodes(p);
    let implicit_run = nodes
        .iter()
        .find(|n| n.text.contains("Implicit linked run"))
        .expect("should find implicit run");
    let explicit_run = nodes
        .iter()
        .find(|n| n.text.contains("Explicit linked char run"))
        .expect("should find explicit run");

    // Both runs should have the same bold state.
    let implicit_bold = implicit_run.marks.contains(&Mark::Bold);
    let explicit_bold = explicit_run.marks.contains(&Mark::Bold);
    assert_eq!(
        implicit_bold, explicit_bold,
        "implicit and explicit linked runs should have the same bold state. \
         Implicit={implicit_bold}, Explicit={explicit_bold}. \
         Per §17.7.4.6, the linked pair should behave identically."
    );

    // Both runs should have the same color.
    assert_eq!(
        implicit_run.style_props.color, explicit_run.style_props.color,
        "implicit and explicit linked runs should have the same color. \
         Per §17.7.4.6, applying the linked char style explicitly should \
         produce the same result as the implicit paragraph style linkage."
    );
}

