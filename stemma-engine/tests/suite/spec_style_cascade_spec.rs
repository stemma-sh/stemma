/// §17.3.1.33: Child style sets line=240 but omits lineRule. Parent has
/// lineRule=exact. Per §17.3.1.33, when lineRule is omitted and line is
/// present, lineRule defaults to "auto" — it should NOT inherit "exact"
/// from the parent style.
///
/// Bug hypothesis: The style chain merges spacing_line_rule via simple
/// overlay (child overrides parent only if child sets it). Since the child
/// doesn't set lineRule, the parent's "exact" leaks through. But per
/// §17.3.1.33, the child's w:spacing element should be treated atomically
/// — when it sets line, lineRule resets to "auto".
#[test]
fn line_rule_child_resets_to_auto_not_inherit_exact() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "line-rule-absent");
    let paras = common::all_paragraphs(&doc);

    let p = paras
        .iter()
        .find(|p| common::paragraph_text(p).contains("BasedOn exact but no lineRule text"))
        .expect("should find StyleNoLineRuleBasedOnExact paragraph");

    let spacing = p
        .spacing
        .as_ref()
        .expect("para should have spacing from StyleNoLineRuleBasedOnExact");
    assert_eq!(
        spacing.line,
        Some(240),
        "line should be 240 from child style (overrides parent's 360)"
    );
    assert_eq!(
        spacing.line_rule,
        Some(LineSpacingRule::Auto),
        "lineRule should be Auto (§17.3.1.33 default when omitted with line present), \
         NOT Exact inherited from parent. Per §17.3.1.33: 'If [lineRule] is omitted, \
         then it shall be assumed to be of a value auto if a line attribute value is present.' \
         Bug: style chain overlay lets parent's Exact leak through because child doesn't \
         explicitly set lineRule."
    );
}

