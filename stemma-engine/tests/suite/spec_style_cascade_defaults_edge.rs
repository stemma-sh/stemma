// 4. Normal style with basedOn chain — rPr inheritance (§17.7.4.3)
//
// Normal (default paragraph style) has basedOn=CustomBase. CustomBase
// sets sz=24, color=FF0000. Normal overrides sz=28. An unstyled paragraph
// should get sz=28 from Normal and color=FF0000 from CustomBase.
// ============================================================================

/// §17.7.4.3 + §17.7.4.17: Normal basedOn CustomBase. Implicit Normal
/// paragraph should get font_size=28 from Normal (overrides CustomBase's 24).
#[test]
fn normal_based_on_font_size() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "normal-with-based-on");
    let paras = common::all_paragraphs(&doc);

    let p = find_para_by_text(&paras, "Implicit Normal with basedOn");
    let nodes = text_nodes(p);
    assert!(!nodes.is_empty(), "paragraph should have text nodes");
    let run = &nodes[0];

    assert_eq!(
        run.style_props.font_size,
        Some(28),
        "implicit Normal (basedOn CustomBase) should resolve font_size=28 \
         from Normal's own rPr (overriding CustomBase's sz=24). \
         Per §17.7.4.3, child style values override parent per-property."
    );
}

/// §17.7.4.3 + §17.7.4.17: Normal basedOn CustomBase. Implicit Normal
