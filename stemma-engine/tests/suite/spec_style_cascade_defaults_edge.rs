//! Spec-compliance tests: docDefaults pPr, Normal basedOn, and style resolution edge cases
//!
//! These tests verify correct behavior for edge cases in:

//!   - docDefaults pPr (alignment, indent, spacing, contextualSpacing)
//!   - Normal style with basedOn chain
//!   - Multiple default paragraph styles
//!   - basedOn edge cases (non-existent, circular, cross-type)
//!
//! Key spec references:
//!   - ISO 29500-1 §17.7.5: docDefaults (rPrDefault, pPrDefault)
//!   - ISO 29500-1 §17.7.4.17: Default styles (w:default="1")
//!   - ISO 29500-1 §17.7.4.3: basedOn chain and type validation
//!   - ISO 29500-1 §17.7.2: Style hierarchy precedence
//!
//! Fixture DOCX files are generated by
//! `testdata/spec-compliance/style-cascade-defaults-edge/create_docs.py`.

use stemma::{Alignment, InlineNode, Mark};

use crate::common;

const FIXTURES: &str = "testdata/spec-compliance/style-cascade-defaults-edge";

// -- helpers -----------------------------------------------------------------

fn text_nodes(p: &stemma::ParagraphNode) -> Vec<stemma::TextNode> {
    p.all_inlines_owned()
        .into_iter()
        .filter_map(|i| match i {
            InlineNode::Text(t) => Some(*t),
            _ => None,
        })
        .collect()
}

fn find_para_by_text<'a>(
    paras: &[&'a stemma::ParagraphNode],
    needle: &str,
) -> &'a stemma::ParagraphNode {
    paras
        .iter()
        .find(|p| common::paragraph_text(p).contains(needle))
        .unwrap_or_else(|| panic!("should find paragraph containing {:?}", needle))
}

