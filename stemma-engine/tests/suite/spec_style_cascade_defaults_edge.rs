/// §17.7.5 + §17.3.1.12: Direct left=0 overrides docDefaults left=720,
/// but right should still come from docDefaults (per-attribute merge).
#[test]
fn docdefaults_ppr_indent_direct_partial_override() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "docdefaults-ppr-indent");
    let paras = common::all_paragraphs(&doc);

    let p = find_para_by_text(&paras, "Direct left zero overrides docDefaults");

    let indent = p.indent.as_ref();
    assert!(
        indent.is_some(),
        "paragraph with direct left=0 should still have indent (at least from docDefaults right)."
    );
    let indent = indent.unwrap();
    assert_eq!(
        indent.left,
        Some(0),
        "direct left=0 should override docDefaults left=720."
    );
    // Per-attribute merge: right should come from docDefaults since direct
    // doesn't set it.
    assert_eq!(
        indent.right,
        Some(360),
        "direct left=0 does not set right — right should come from docDefaults (360). \
         Per §17.3.1.12, indentation merges per-attribute."
    );
}

