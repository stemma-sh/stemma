// ============================================================================
// 8. Cross-type basedOn (§17.7.4.3)
//
// Paragraph style CrossTypePara has basedOn pointing to character style
// CharParent. Per §17.7.4.3, this cross-type reference should be ignored
// (the chain stops at the type mismatch).
// ============================================================================

/// §17.7.4.3: CrossTypePara basedOn CharParent (character style). The
/// cross-type reference should be ignored — CharParent's color and sz
/// should NOT leak into the paragraph style.
#[test]
fn cross_type_based_on_ignored() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "cross-type-based-on");
    let paras = common::all_paragraphs(&doc);

    let p = find_para_by_text(&paras, "Cross type basedOn para");
    let nodes = text_nodes(p);
    assert!(!nodes.is_empty(), "paragraph should have text nodes");
    let run = &nodes[0];

    // CrossTypePara sets bold in its own rPr. CharParent (char style) sets
    // color=FF0000 and sz=32. The cross-type basedOn should be ignored.
    assert!(
        run.marks.contains(&Mark::Bold),
        "CrossTypePara sets bold=on in its own rPr — should still work."
    );

    // CharParent's color should NOT leak through the cross-type basedOn.
    assert_eq!(
        run.style_props.color.as_deref(),
        None,
        "CrossTypePara basedOn CharParent (character style) — per §17.7.4.3, \
         cross-type basedOn is ignored. CharParent's color=FF0000 should NOT \
         leak into the paragraph style."
    );

    // CharParent's font_size (32) should NOT leak through the cross-type basedOn.
    // The template's docDefaults rPr has sz=22, which is the expected fallback.
    assert_ne!(
        run.style_props.font_size,
        Some(32),
        "CrossTypePara basedOn CharParent — CharParent's sz=32 should NOT \
         leak into the paragraph style per §17.7.4.3."
    );
}

