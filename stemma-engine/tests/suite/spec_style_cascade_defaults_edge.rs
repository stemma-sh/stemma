// ============================================================================
// 5. Multiple paragraph styles with default="1" (§17.7.4.17)
//
// Per spec, only one style per type should have default="1". When two
// exist, the code should not crash. We test that it handles this gracefully.
// ============================================================================

/// §17.7.4.17: Two paragraph styles with default="1" — code should not crash.
/// The unstyled paragraph should get properties from one of them.
#[test]
fn multiple_defaults_does_not_crash() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "multiple-default-para-styles");
    let paras = common::all_paragraphs(&doc);

    let p = find_para_by_text(&paras, "Unstyled with two defaults");
    let nodes = text_nodes(p);
    assert!(!nodes.is_empty(), "paragraph should have text nodes");
    let run = &nodes[0];

    // The code should pick one of the two defaults. We verify it doesn't crash
    // and that font_size is set to one of the two values (20 or 28).
    let font_size = run.style_props.font_size;
    assert!(
        font_size == Some(20) || font_size == Some(28),
        "with two default paragraph styles (sz=20 and sz=28), unstyled paragraph \
         should get font_size from one of them. Got: {:?}",
        font_size
    );

    // Similarly, color should be from one of the two defaults.
    let color = run.style_props.color.as_deref();
    assert!(
        color == Some("FF0000") || color == Some("0000FF"),
        "with two default paragraph styles, color should be from one of them. Got: {:?}",
        color
    );
}

