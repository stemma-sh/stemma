//! Spec-compliance tests: Tab Stop / Indentation Interaction (ISO 29500-1 §17.3.1.12 + §17.3.1.38)
//!
//! These tests verify that indentation is correctly computed when tab stops and
//! literal prefix stripping interact.
//!
//! ## Model invariant (see `Indentation` doc in domain.rs)
//!
//! `first_line` always represents the OOXML cascade value (§17.3.1.12),
//! resolved via direct > numbering > style per-attribute merge.
//!
//! Prefix stripping is a **content transformation** — it splits inline text
//! into `literal_prefix` + body.  It does NOT change the paragraph's indent.
//! The frontend renders the prefix inline (::before) and uses `text-indent`
//! (driven by `first_line`) to position the entire first line.
//!
//! Fixture DOCX files are generated by `testdata/spec-compliance/create_docs.py`.

use crate::common;

const FIXTURES: &str = "testdata/spec-compliance/tab-indent-interaction";

// ── prefix stripped + negative indent ────────────────────────────────────

/// ISO 29500-1 §17.3.1.12: w:left defines the indentation for continuation
/// lines (all lines except the first). w:firstLine (or w:hanging) defines the
/// additional offset of the first line relative to w:left.
///
/// Fixture: w:left=-720, w:right=-360, tab at 360, text "(a)\tEquity Financing..."
///
/// After prefix stripping, first_line = cascade value.  This fixture has no
/// firstLine in the style chain, so first_line = None (or 0).
/// The prefix is rendered inline by the frontend.
#[test]
fn spec_prefix_stripped_preserves_continuation_indent() {
    let (_runtime, doc) = common::import_fixture(FIXTURES, "prefix-negative-indent");
    let paras = common::all_paragraphs(&doc);
    assert!(!paras.is_empty(), "expected at least 1 paragraph");

    let p = &paras[0];
    let indent = p.indent.as_ref().expect(
        "paragraph should have indent. w:left=-720 is set but indent is None after import.",
    );

    // Continuation lines use w:left.
    assert_eq!(
        indent.left,
        Some(-720),
        "left should be -720 (w:left from the paragraph). Got left={:?}",
        indent.left,
    );

    // first_line = cascade value. No firstLine in direct or style → None.
    assert!(
        indent.effective_first_line_twips.is_none() || indent.effective_first_line_twips == Some(0),
        "first_line should be None or 0 (no firstLine in style cascade). \
         Got first_line={:?}",
        indent.effective_first_line_twips,
    );

    // Right indent should be preserved unchanged.
    assert_eq!(
        indent.right,
        Some(-360),
        "right should be -360 (from w:right). Got right={:?}",
        indent.right,
    );
}

