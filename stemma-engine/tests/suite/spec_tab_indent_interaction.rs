// ── tab absorption with zero firstLine ───────────────────────────────────

/// ISO 29500-1 §17.3.1.12: When firstLine is absent (or 0) and body text
/// contains tabs, the tab absorption branch (runtime.rs:3519) should NOT fire.
/// The condition `first_line_twips != 0` gates this branch, so the indent
/// should pass through unchanged.
///
/// Fixture: w:left=720, no firstLine, text "A\tB"
///
/// Expected: left=720, first_line=None (passthrough).
#[test]
fn spec_tab_absorption_preserves_indent_when_no_firstline() {
    let (_runtime, doc) = common::import_fixture(FIXTURES, "tab-absorption-zero-firstline");
    let paras = common::all_paragraphs(&doc);
    assert!(!paras.is_empty(), "expected at least 1 paragraph");

    let p = &paras[0];
    let indent = p.indent.as_ref().expect("paragraph should have indent");

    assert_eq!(
        indent.left,
        Some(720),
        "left should be 720 (w:left, passthrough — tab absorption should not fire \
         when firstLine is 0). Got left={:?}",
        indent.left,
    );

    assert!(
        indent.effective_first_line_twips.is_none() || indent.effective_first_line_twips == Some(0),
        "first_line should be None or 0 (passthrough). Got first_line={:?}",
        indent.effective_first_line_twips,
    );
}

// ── prefix with space separator ──────────────────────────────────────────

/// ISO 29500-1 §17.3.1.12 + §17.3.1.38: When a prefix is separated by a
/// space (not a tab), no tab stop is consumed by the prefix. The body_left
/// computation (which depends on a tab being consumed) should not apply.
///
/// Fixture: w:left=360, tab at 720, text "(a) Body text..."
///
/// Expected: If prefix is stripped, indent should still reflect w:left=360
/// because no tab was consumed by the prefix. The tab stop at 720 remains
/// available for body text tabs.
#[test]
fn spec_prefix_space_separator_indent() {
    let (_runtime, doc) = common::import_fixture(FIXTURES, "prefix-space-separator");
    let paras = common::all_paragraphs(&doc);
    assert!(!paras.is_empty(), "expected at least 1 paragraph");

    let p = &paras[0];
    let indent = p.indent.as_ref().expect("paragraph should have indent");

    // Whether or not the prefix "(a) " is stripped, the indent should reflect
    // the resolved w:left=360. A space separator does not consume a tab stop,
    // so body_left should not be computed from tab_stops_abs.first().
    assert_eq!(
        indent.left,
        Some(360),
        "left should be 360 (w:left). Space-separated prefix should not trigger \
         tab-stop-based body_left computation. Got left={:?}",
        indent.left,
    );
}

// ── negative indent + tab, no prefix ─────────────────────────────────────

/// ISO 29500-1 §17.3.1.12: When w:left is negative and the text starts with
/// a tab but does not match a prefix pattern, no prefix stripping occurs.
/// The tab absorption branch only fires when first_line_twips != 0, which
/// is false here (no firstLine set). The indent should pass through.
///
/// Fixture: w:left=-360, tab at 720, text "\tBody text..."
///
/// Expected: left=-360, first_line=None (passthrough).
#[test]
fn spec_negative_indent_tab_no_prefix() {
    let (_runtime, doc) = common::import_fixture(FIXTURES, "negative-indent-no-prefix");
    let paras = common::all_paragraphs(&doc);
    assert!(!paras.is_empty(), "expected at least 1 paragraph");

    let p = &paras[0];
    let indent = p.indent.as_ref().expect("paragraph should have indent");

    assert_eq!(
        indent.left,
        Some(-360),
        "left should be -360 (w:left, passthrough — no prefix to strip, \
         tab absorption gated on first_line_twips!=0). Got left={:?}",
        indent.left,
    );

    assert!(
        indent.effective_first_line_twips.is_none() || indent.effective_first_line_twips == Some(0),
        "first_line should be None (no firstLine set, passthrough). Got first_line={:?}",
        indent.effective_first_line_twips,
    );
}
