// =========================================================================
// 4. rPrChange preserves previous run formatting (adding bold)
//    ECMA-376 §17.13.5.31
// =========================================================================

/// ECMA-376 §17.13.5.31: When run formatting changes (normal -> bold)
/// alongside a text edit, rPrChange should record the previous state (no bold).
///
/// Old: normal text "Bold test text here."
/// New: bold text "Bold test text."
///
/// The unchanged portion "Bold test text" should get an rPrChange showing
/// the previous state without bold. The diff treats this as modified text
/// (due to the "here." deletion) and detects the formatting change on the
/// overlapping portion.
#[test]
fn spec_rprchange_preserves_previous_bold() {
    let redline_bytes = redline_from_pair("bold-change");
    let xml = extract_document_xml(&redline_bytes);
    let root = parse_xml(&xml);

    let mut rpr_changes = Vec::new();
    find_all_w_elements(&root, "rPrChange", &mut rpr_changes);
    assert!(
        !rpr_changes.is_empty(),
        "redline XML should contain at least one w:rPrChange element \
         when run formatting changes from normal to bold. The diff should \
         detect a formatting difference on the unchanged text portion."
    );

    // The inner rPr of rPrChange should NOT have w:b (text was not bold before).
    let inner_rpr = find_w_child(rpr_changes[0], "rPr")
        .expect("rPrChange should have a child w:rPr element per §17.13.5.31");
    let bold = find_w_child(inner_rpr, "b");
    assert!(
        bold.is_none(),
        "inner rPr of rPrChange should NOT have w:b since the text was not bold \
         before. The previous state had no bold formatting."
    );

    // The outer rPr (on the run itself) SHOULD have w:b (current state is bold).
    let mut runs = Vec::new();
    find_all_w_elements(&root, "r", &mut runs);
    let run_with_bold = runs.iter().find(|r| {
        find_w_child(r, "rPr")
            .and_then(|rpr| find_w_child(rpr, "b"))
            .is_some()
    });
    assert!(
        run_with_bold.is_some(),
        "at least one run in the redline output should have w:b (the current bold state)"
    );

    // rPrChange must have w:author and w:id attributes.
    assert!(
        attr_value(rpr_changes[0], "w:author").is_some()
            || attr_value(rpr_changes[0], "author").is_some(),
        "rPrChange should have a w:author attribute"
    );
}

// =========================================================================
// 5. pPrChange is a COMPLETE snapshot, not a delta
//    ECMA-376 §17.13.5.29
// =========================================================================

/// ECMA-376 §17.13.5.29: The child pPr of pPrChange is a COMPLETE snapshot
/// of the previous paragraph properties, not just the properties that changed.
///
/// Old: left-aligned, spacing before=240, keepNext=true, "Complete snapshot test here."
/// New: center-aligned, spacing before=240, keepNext=true, "Complete snapshot test."
/// (only alignment and text changed)
///
/// The pPrChange should record ALL previous properties:
/// - jc = left (alignment changed)
/// - spacing before = 240 (unchanged but should be in the snapshot)
/// - keepNext (unchanged but should be in the snapshot)
///
/// Previously a known gap -- pPrChange now emits a complete snapshot
/// including properties like keepNext.
#[test]
fn spec_pprchange_is_complete_snapshot_not_delta() {
    let redline_bytes = redline_from_pair("complete-snapshot");
    let xml = extract_document_xml(&redline_bytes);
    let root = parse_xml(&xml);

    let mut ppr_changes = Vec::new();
    find_all_w_elements(&root, "pPrChange", &mut ppr_changes);
    assert!(
        !ppr_changes.is_empty(),
        "redline XML should contain at least one w:pPrChange element"
    );

    let inner_ppr =
        find_w_child(ppr_changes[0], "pPr").expect("pPrChange should have a child w:pPr element");

    // Check 1: previous alignment should be recorded (this property changed).
    let jc = find_w_child(inner_ppr, "jc");
    assert!(
        jc.is_some(),
        "inner pPr should have w:jc recording previous alignment"
    );
    let jc_val = attr_value(jc.unwrap(), "w:val").or_else(|| attr_value(jc.unwrap(), "val"));
    assert_eq!(jc_val, Some("left"), "previous alignment should be 'left'");

    // Check 2: previous spacing should be recorded (did NOT change, but per
    // §17.13.5.29 the inner pPr is a complete snapshot of the previous state).
    let spacing = find_w_child(inner_ppr, "spacing");
    assert!(
        spacing.is_some(),
        "inner pPr should have w:spacing recording previous spacing (before=240), \
         even though spacing did not change. Per §17.13.5.29, pPrChange is a \
         complete snapshot of the previous state."
    );
    let before_val =
        attr_value(spacing.unwrap(), "w:before").or_else(|| attr_value(spacing.unwrap(), "before"));
    assert_eq!(
        before_val,
        Some("240"),
        "previous spacing before should be '240'"
    );

    // Check 3: keepNext should be recorded (did NOT change, but should be in snapshot).
    // This is the key gap: our model does not capture keepNext in ParagraphFormattingChange.
    let keep_next = find_w_child(inner_ppr, "keepNext");
    assert!(
        keep_next.is_some(),
        "inner pPr should have w:keepNext recording that the previous paragraph \
         had keepNext=true. Per §17.13.5.29, the child pPr of pPrChange must be \
         a COMPLETE snapshot of previous properties, not just the changed ones. \
         Our ParagraphFormattingChange does not model keepNext, so this is lost."
    );
}

// =========================================================================
// 6. Cell formatting change (tcPrChange) on cell shading
//    ECMA-376 §17.13.5.36
// =========================================================================

/// ECMA-376 §17.13.5.36: When cell properties change (shading added) alongside
/// a text edit, tcPrChange should capture the previous cell state.
///
/// Old: table with unshaded cell, "Cell content here."
/// New: cell gets yellow shading, "Cell content."
///
/// This test documents a known gap: our diff engine does not track cell-level
/// formatting changes during diff_and_redline. CellFormattingChange is only
/// populated during import of DOCX files that already contain w:tcPrChange.
#[test]
fn spec_formatting_change_on_table_cell_properties() {
    let redline_bytes = redline_from_pair("cell-shading-change");
    let xml = extract_document_xml(&redline_bytes);
    let root = parse_xml(&xml);

    let mut tc_pr_changes = Vec::new();
    find_all_w_elements(&root, "tcPrChange", &mut tc_pr_changes);
    assert!(
        !tc_pr_changes.is_empty(),
        "redline XML should contain at least one w:tcPrChange element \
         when cell formatting changes (shading added). Currently, our diff \
         engine does not track cell formatting changes."
    );

    // The inner tcPr of tcPrChange should NOT have w:shd (cell was unshaded before).
    let inner_tc_pr = find_w_child(tc_pr_changes[0], "tcPr")
        .expect("tcPrChange should have a child w:tcPr element");
    let shd = find_w_child(inner_tc_pr, "shd");
    assert!(
        shd.is_none(),
        "inner tcPr of tcPrChange should NOT have w:shd since the cell was \
         unshaded before"
    );
}
