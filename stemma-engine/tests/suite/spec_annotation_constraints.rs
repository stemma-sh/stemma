/// ISO 29500-1 §17.13.4.2/3: commentRangeStart and commentRangeEnd markers
/// with id="1" should be present in the body, along with a commentReference
/// with id="1".
///
/// Happy path: the fixture has all three inline markers for comment id=1.
#[test]
fn spec_comment_range_has_matching_reference() {
    let (_runtime, doc) = common::import_fixture(FIXTURES, "comment-ref-integrity");

    let paras = common::all_paragraphs(&doc);
    assert!(!paras.is_empty(), "should have at least one paragraph");

    let all_inlines: Vec<&InlineNode> = paras.iter().flat_map(|p| p.all_inlines()).collect();

    let has_range_start = all_inlines
        .iter()
        .any(|i| matches!(i, InlineNode::CommentRangeStart { id } if id == "1"));
    let has_range_end = all_inlines
        .iter()
        .any(|i| matches!(i, InlineNode::CommentRangeEnd { id } if id == "1"));
    let has_reference = all_inlines
        .iter()
        .any(|i| matches!(i, InlineNode::CommentReference { id } if id == "1"));

    // CommentReference may also be stored as an OpaqueInline with
    // OpaqueKind::CommentReference — check that form too.
    let has_opaque_reference = all_inlines.iter().any(|i| match i {
        InlineNode::OpaqueInline(o) => matches!(
            &o.kind,
            OpaqueKind::CommentReference(ref_data) if ref_data.reference_id == "1"
        ),
        _ => false,
    });

    // Also check for Decoration-based representation (alternative encoding)
    let has_decoration_range = all_inlines.iter().any(
        |i| matches!(i, InlineNode::Decoration(d) if d.kind == stemma::DecorationType::CommentRange),
    );

    assert!(
        has_range_start || has_decoration_range,
        "body should contain CommentRangeStart{{id: \"1\"}} or CommentRange decoration. \
         Found inline discriminants: {:?}",
        all_inlines
            .iter()
            .map(|i| format!("{:?}", std::mem::discriminant(*i)))
            .collect::<Vec<_>>()
    );

    assert!(
        has_range_end || has_decoration_range,
        "body should contain CommentRangeEnd{{id: \"1\"}} or CommentRange decoration. \
         Found inline discriminants: {:?}",
        all_inlines
            .iter()
            .map(|i| format!("{:?}", std::mem::discriminant(*i)))
            .collect::<Vec<_>>()
    );

    assert!(
        has_reference || has_opaque_reference,
        "body should contain CommentReference{{id: \"1\"}} (either as InlineNode::CommentReference \
         or OpaqueInline with OpaqueKind::CommentReference). \
         Found inlines: {:?}",
        all_inlines
    );
}

