/// ISO 29500-1 §17.16.2: A properly balanced complex field produces
/// fldChar opaques in order: Begin, Instruction, Separate, End.
///
/// The fixture has: fldChar(begin) -> instrText("DATE") -> fldChar(separate)
/// -> result text -> fldChar(end). All four field-related opaques should
/// appear in the correct order.
#[test]
fn spec_field_begin_separate_end_balanced() {
    let (_runtime, doc) = common::import_fixture(FIXTURES, "field-balance");

    let paras = common::all_paragraphs(&doc);
    assert!(
        paras.len() >= 2,
        "should have at least 2 paragraphs (field paragraph + normal text), got {}",
        paras.len()
    );

    // The first paragraph contains the complex field
    let fields = collect_field_opaques(paras[0]);

    assert!(
        !fields.is_empty(),
        "first paragraph should contain Field opaques for the complex field. \
         Inlines: {:?}",
        paras[0]
            .all_inlines()
            .map(|i| format!("{:?}", std::mem::discriminant(i)))
            .collect::<Vec<_>>()
    );

    let has_begin = fields.iter().any(|fd| fd.field_kind == FieldKind::Begin);
    let has_separate = fields.iter().any(|fd| fd.field_kind == FieldKind::Separate);
    let has_end = fields.iter().any(|fd| fd.field_kind == FieldKind::End);

    assert!(
        has_begin,
        "complex field should have FieldKind::Begin. Found kinds: {:?}",
        fields.iter().map(|fd| &fd.field_kind).collect::<Vec<_>>()
    );
    assert!(
        has_separate,
        "complex field should have FieldKind::Separate. Found kinds: {:?}",
        fields.iter().map(|fd| &fd.field_kind).collect::<Vec<_>>()
    );
    assert!(
        has_end,
        "complex field should have FieldKind::End. Found kinds: {:?}",
        fields.iter().map(|fd| &fd.field_kind).collect::<Vec<_>>()
    );

    // Verify ordering: Begin must come before Separate, Separate before End
    let kind_sequence: Vec<&FieldKind> = fields.iter().map(|fd| &fd.field_kind).collect();
    let begin_pos = kind_sequence
        .iter()
        .position(|k| **k == FieldKind::Begin)
        .expect("Begin should exist");
    let separate_pos = kind_sequence
        .iter()
        .position(|k| **k == FieldKind::Separate)
        .expect("Separate should exist");
    let end_pos = kind_sequence
        .iter()
        .position(|k| **k == FieldKind::End)
        .expect("End should exist");

    assert!(
        begin_pos < separate_pos,
        "Begin (pos {}) should come before Separate (pos {})",
        begin_pos,
        separate_pos
    );
    assert!(
        separate_pos < end_pos,
        "Separate (pos {}) should come before End (pos {})",
        separate_pos,
        end_pos
    );
}

