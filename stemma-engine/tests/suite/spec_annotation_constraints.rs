/// ISO 29500-1 §17.11.2: A footnoteReference in the body must link to a
/// FootnoteStory with the same ID.
///
/// Happy path: the fixture has footnoteReference(id=1) in the body and a
/// matching footnote(id=1) in footnotes.xml. Both should be present in
/// the parsed CanonDoc.
#[test]
fn spec_footnote_reference_has_matching_story() {
    let (_runtime, doc) = common::import_fixture(FIXTURES, "footnote-ref-integrity");

    // (a) Body should contain a FootnoteReference with reference_id="1"
    let paras = common::all_paragraphs(&doc);
    assert!(!paras.is_empty(), "should have at least one paragraph");

    let footnote_ref_id = paras.iter().find_map(|p| {
        p.all_inlines().find_map(|inline| {
            if let InlineNode::OpaqueInline(oi) = inline
                && let OpaqueKind::FootnoteReference(ref_data) = &oi.kind
            {
                return Some(ref_data.reference_id.clone());
            }
            None
        })
    });

    assert_eq!(
        footnote_ref_id.as_deref(),
        Some("1"),
        "body should contain a FootnoteReference opaque with reference_id=\"1\""
    );

    // (b) CanonDoc.footnotes should contain a FootnoteStory with id="1"
    let fn1 = doc.footnotes.iter().find(|fn_| fn_.id == "1");
    assert!(
        fn1.is_some(),
        "CanonDoc.footnotes should contain FootnoteStory with id=\"1\", \
         got ids: {:?}",
        doc.footnotes.iter().map(|n| &n.id).collect::<Vec<_>>()
    );

    let fn1 = fn1.unwrap();
    assert_eq!(
        fn1.note_type,
        NoteType::Normal,
        "footnote id=1 should be NoteType::Normal"
    );

    let text = blocks_text(&fn1.blocks);
    assert!(
        text.contains("This is the footnote"),
        "footnote content should contain 'This is the footnote', got: '{}'",
        text
    );
}

