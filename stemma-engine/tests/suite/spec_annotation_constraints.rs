//! Spec-compliance tests: Annotation and tracked change behavioral constraints
//! (ISO 29500-1 §17.11.2, §17.16.2, §17.13.4)
//!
//! These tests verify cross-validation and balancing constraints:
//!   - Footnote reference integrity: footnoteReference must have matching FootnoteStory
//!   - Field code balance: complex fields must be properly balanced (begin/separate/end)
//!   - Comment referential integrity: comment range/reference must have matching CommentStory
//!
//! Tests marked `#[ignore]` document known gaps — they encode the *correct* spec
//! behavior and are expected to fail against the current implementation.
//!
//! Fixture DOCX files are generated by
//! `testdata/spec-compliance/create_annotation_constraints.py`.

use stemma::{BlockNode, FieldData, FieldKind, InlineNode, NoteType, OpaqueKind};

use crate::common;

const FIXTURES: &str = "testdata/spec-compliance/annotation-constraints";

// ── helpers ──────────────────────────────────────────────────────────────

/// Collect all FieldData from OpaqueInline nodes in a paragraph.
fn collect_field_opaques(para: &stemma::ParagraphNode) -> Vec<&FieldData> {
    let mut fields = Vec::new();
    for inline in para.all_inlines() {
        if let InlineNode::OpaqueInline(opaque) = inline
            && let OpaqueKind::Field(ref fd) = opaque.kind
        {
            fields.push(fd);
        }
    }
    fields
}

/// Extract plain text from tracked blocks.
fn blocks_text(blocks: &[stemma::TrackedBlock]) -> String {
    let mut text = String::new();
    for tb in blocks {
        if let BlockNode::Paragraph(p) = &tb.block {
            for inline in p.all_inlines() {
                if let InlineNode::Text(t) = inline {
                    text.push_str(&t.text);
                }
            }
        }
    }
    text
}

