// 3. Linked style missing properties (§17.7.4.6)
//
// When a paragraph style (HeadingPara) has w:link to a character style
// (HeadingChar), runs inherit from the linked character style. But if the
// linked char style doesn't set font_size and the para style does, the
// font_size should still be available (from the para style's own rPr).
//
// Our implementation replaces the para style's resolved marks entirely
// with the linked char style's marks, losing properties the char style
// doesn't set.
// ═══════════════════════════════════════════════════════════════════════════

/// §17.7.4.6: Linked char style has bold but no font_size. The paragraph
/// style has both bold and font_size=32. Runs should get font_size=32.
///
/// Bug: Our code replaces para_styles[HeadingPara] entirely with
/// char_styles[HeadingChar], which doesn't have font_size. The font_size
/// from HeadingPara's own rPr is lost.
#[test]
fn linked_style_preserves_para_font_size() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "linked-style-missing-props");
    let paras = common::all_paragraphs(&doc);

    let p = paras
        .iter()
        .find(|p| common::paragraph_text(p).contains("Heading with linked style"))
        .expect("should find paragraph with HeadingPara style");

    let nodes = text_nodes(p);
    let run = &nodes[0];

    // The paragraph style HeadingPara has sz=32 in its rPr.
    // The linked char style HeadingChar has bold but NO sz.
    // The run should get font_size=32 from HeadingPara.
    assert_eq!(
        run.style_props.font_size,
        Some(32),
        "paragraph with HeadingPara (linked to HeadingChar) should get \
         font_size=32 from HeadingPara's rPr. The linked char style has no \
         sz, so the para style's own font_size should be used. \
         Bug: linked style replacement drops unset properties."
    );
}

