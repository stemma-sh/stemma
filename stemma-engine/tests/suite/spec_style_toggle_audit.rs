// ═══════════════════════════════════════════════════════════════════════════
// 2. Doc defaults bold=on + style bold=off (§17.7.3 + MS-OI29500 §2.1.230)
//
// ECMA §17.7.3: "If the value specified by the document defaults is true,
// the effective value is true." — This means doc defaults on = ALWAYS on.
// MS-OI29500 §2.1.230a: Word's behavior differs — the effective value is
// true only if false for an even number of levels.
//
// Our code shortcuts: if doc_default is On, return On immediately.
// This matches the ECMA letter but prevents styles from turning off bold.
// ═══════════════════════════════════════════════════════════════════════════

/// §17.7.3 + MS-OI29500 §2.1.230: docDefaults bold=on, para style bold=off.
/// The para style explicitly says "not bold" — this should be honored.
///
/// Our shortcut (doc_default On → always On) prevents the style's bold=off
/// from taking effect. This is a behavioral violation against both Word
/// and reasonable user expectations.
#[test]
fn toggle_doc_defaults_on_style_off() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "toggle-doc-defaults-on");
    let paras = common::all_paragraphs(&doc);

    let p = paras
        .iter()
        .find(|p| common::paragraph_text(p).contains("Explicitly not bold"))
        .expect("should find paragraph with StyleNoBold");

    let nodes = text_nodes(p);
    let run = &nodes[0];

    // Word behavior: the paragraph style's bold=off should win.
    // MS-OI29500 §2.1.230a: Word determines effective value based on
    // even/odd count of levels setting the property.
    // Para style says off → effective is off.
    assert!(
        !run.marks.contains(&Mark::Bold),
        "docDefaults bold=on + para style bold=off should produce bold=OFF. \
         The paragraph style explicitly disables bold. MS-OI29500 §2.1.230a \
         says Word does not unconditionally force doc defaults on. \
         Our shortcut (doc_default On → always On) is wrong here."
    );
}

