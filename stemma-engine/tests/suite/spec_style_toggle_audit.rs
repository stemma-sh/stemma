//! Spec-compliance tests: Style Toggle Properties & Cascade (ISO 29500-1 §17.7.3)
//!
//! These tests verify toggle property XOR semantics, style cascade resolution,
//! linked style behavior, and doc defaults interactions from the OOXML specification.
//!
//! Key spec references:
//! - §17.7.3: Toggle property XOR semantics across hierarchy levels
//! - §17.7.1: basedOn chain resolution (first-value-encountered for toggles)
//! - §17.7.2: Style hierarchy: docDefaults → table → para → char → direct
//! - §17.7.4.6: Linked style pairs (paragraph + character)
//! - §17.7.4.17: Implicit Normal style for unstyled paragraphs
//! - §17.7.5: docDefaults (rPrDefault/pPrDefault)
//! - MS-OI29500 §2.1.230: Word's toggle behavior with doc defaults
//! - MS-OI29500 §2.1.258: Word resets toggles instead of XORing
//!
//! Fixture DOCX files are generated by
//! `testdata/spec-compliance/style-toggle-audit/create_docs.py`.

use stemma::{InlineNode, Mark};

use crate::common;

const FIXTURES: &str = "testdata/spec-compliance/style-toggle-audit";

// ── helpers ──────────────────────────────────────────────────────────────

fn text_nodes(p: &stemma::ParagraphNode) -> Vec<stemma::TextNode> {
    p.all_inlines_owned()
        .into_iter()
        .filter_map(|i| match i {
            InlineNode::Text(t) => Some(*t),
            _ => None,
        })
        .collect()
}

// ═══════════════════════════════════════════════════════════════════════════
// 1. Toggle XOR between paragraph style and character style (§17.7.3)
//
// ECMA-376 §17.7.3 says: when toggle properties appear at multiple levels
// of the style hierarchy, they are combined with XOR.
// MS-OI29500 §2.1.258 says: Word RESETS the toggle to the value specified
// by the paragraph style (simple override, not XOR).
//
// Our implementation does XOR (resolve_toggle_mark), which follows ECMA
// but diverges from Word.
// ═══════════════════════════════════════════════════════════════════════════

/// §17.7.3: Para style bold=on + char style bold=on.
/// ECMA spec: XOR → on ^ on = off.
/// Word behavior (MS-OI29500 §2.1.258): bold=on (reset, not XOR).
///
/// This test asserts Word-compatible behavior (bold=ON). Our XOR-based
/// resolve_toggle_mark produces bold=OFF, which is a behavioral violation
/// for Word-compatibility but follows the ECMA-376 letter.
#[test]
fn toggle_xor_para_char_both_bold_on() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "toggle-xor-para-char");
    let paras = common::all_paragraphs(&doc);

    let p = paras
        .iter()
        .find(|p| common::paragraph_text(p).contains("Both para and char bold"))
        .expect("should find paragraph with both styles");

    let nodes = text_nodes(p);
    assert!(!nodes.is_empty(), "paragraph should have text nodes");
    let run = &nodes[0];

    // Word behavior: bold=on (reset semantics per MS-OI29500 §2.1.258).
    // Our current ECMA-spec XOR produces bold=off (on ^ on = off).
    // This test documents the Word-compatible expectation.
    assert!(
        run.marks.contains(&Mark::Bold),
        "para style bold=on + char style bold=on should produce bold=ON \
         (Word resets toggle per MS-OI29500 §2.1.258). Our XOR implementation \
         produces bold=OFF which diverges from Word behavior."
    );
}

