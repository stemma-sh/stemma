// ═══════════════════════════════════════════════════════════════════════════
// 10. Multiple toggle properties independent behavior (§17.7.3)
//
// Different toggle properties should behave independently. Bold and italic
// are both toggles, but XOR for bold shouldn't affect italic.
// ═══════════════════════════════════════════════════════════════════════════

/// §17.7.3: Para style bold=on, italic=on + char style bold=on.
/// Bold: XOR para(on) ^ char(on) = off per ECMA, on per Word.
/// Italic: only para sets it → on (no XOR needed).
#[test]
fn toggle_multi_bold_xor_italic_independent() {
    let (_rt, doc) = common::import_fixture(FIXTURES, "toggle-multi-property");
    let paras = common::all_paragraphs(&doc);

    let p = paras
        .iter()
        .find(|p| common::paragraph_text(p).contains("Multi toggle test"))
        .expect("should find multi-toggle paragraph");

    let nodes = text_nodes(p);
    let run = &nodes[0];

    // Italic should be ON regardless of bold XOR behavior.
    // Only para style sets italic, char style doesn't touch it.
    assert!(
        run.marks.contains(&Mark::Italic),
        "italic should be ON — only para style sets it, char style doesn't. \
         Per §17.7.3, each toggle property is independent."
    );

    // Bold: XOR gives off (ECMA), reset gives on (Word).
    // This test documents the Word-compatible expectation.
    assert!(
        run.marks.contains(&Mark::Bold),
        "para style bold=on + char style bold=on should produce bold=ON \
         per Word behavior (MS-OI29500 §2.1.258 reset semantics). \
         ECMA §17.7.3 XOR would give bold=OFF."
    );
}
